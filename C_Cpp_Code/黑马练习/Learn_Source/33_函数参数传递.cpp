/*
 * ============================================================
 * 文件名: 33_函数参数传递.cpp
 * 主题: C++ 函数参数传递的三种方式
 * ============================================================
 * 本文件涵盖:
 *   1. 值传递 (Pass by Value) - 不修改原值
 *   2. 地址传递 (Pass by Address) - 通过指针修改原值
 *   3. 引用传递 (Pass by Reference) - 通过引用修改原值
 *   4. 三种方式的对比与 swap 交换示例
 * ============================================================
 */

#include <iostream>
using namespace std;

// ============================================================
// 1. 值传递 (Pass by Value) 的函数
// ============================================================
// 值传递: 将实参的值复制一份给形参
// 形参的修改不会影响实参!
void swapByValue(int a, int b) {
    cout << "  [值传递] 交换前: a=" << a << ", b=" << b << endl;
    int temp = a;
    a = b;
    b = temp;
    cout << "  [值传递] 交换后: a=" << a << ", b=" << b << endl;
    // 函数结束后, a 和 b 的改变不会影响实参!
}

// ============================================================
// 2. 地址传递 (Pass by Address) 的函数
// ============================================================
// 地址传递: 传递变量的地址 (指针)
// 通过解引用指针可以直接修改实参的值
void swapByAddress(int* a, int* b) {
    // a 和 b 是指针, 存储的是实参的地址
    cout << "  [地址传递] 交换前: *a=" << *a << ", *b=" << *b << endl;
    int temp = *a;   // 解引用: 获取指针指向的值
    *a = *b;         // 通过指针修改实参的值
    *b = temp;
    cout << "  [地址传递] 交换后: *a=" << *a << ", *b=" << *b << endl;
    // 通过指针修改了实参的值!
}

// ============================================================
// 3. 引用传递 (Pass by Reference) 的函数
// ============================================================
// 引用传递: 使用引用 (&) 直接绑定到实参
// 引用是实参的别名, 对引用的修改就是对实参的修改
void swapByReference(int& a, int& b) {
    // a 和 b 是实参的引用 (别名)
    cout << "  [引用传递] 交换前: a=" << a << ", b=" << b << endl;
    int temp = a;
    a = b;
    b = temp;
    cout << "  [引用传递] 交换后: a=" << a << ", b=" << b << endl;
    // 直接修改了实参!
}

// ============================================================
// 4. 演示: 值传递不修改原值
// ============================================================
void doubleValue(int x) {
    x = x * 2;  // 只修改了形参 x 的副本
    cout << "  函数内部 x = " << x << endl;
}

void doubleAddress(int* x) {
    *x = (*x) * 2;  // 通过指针修改了实参
    cout << "  函数内部 *x = " << *x << endl;
}

void doubleReference(int& x) {
    x = x * 2;  // 通过引用修改了实参
    cout << "  函数内部 x = " << x << endl;
}

// ============================================================
// 5. 演示: 数组的传递 (数组名本质上是地址)
// ============================================================
// 数组作为参数时, 实际上是地址传递
void printArray(int arr[], int size) {
    // arr 实际上是一个指针, 指向数组首元素
    cout << "  数组元素: ";
    for (int i = 0; i < size; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
}

void modifyArray(int arr[], int size) {
    // 因为是地址传递, 可以修改原数组
    for (int i = 0; i < size; i++) {
        arr[i] = arr[i] * 2;  // 每个元素翻倍
    }
}

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 值传递 vs 地址传递 vs 引用传递 - swap 对比
    // ----------------------------------------------------------
    cout << "===== 三种参数传递方式的 swap 对比 =====" << endl;

    int x = 10, y = 20;
    cout << "\n原始值: x=" << x << ", y=" << y << endl;

    // 1.1 值传递: 不能交换原值
    cout << "\n--- 值传递 ---" << endl;
    swapByValue(x, y);
    cout << "  函数调用后: x=" << x << ", y=" << y << endl;
    // x 和 y 没有改变! x=10, y=20

    // 1.2 地址传递: 可以交换原值
    cout << "\n--- 地址传递 ---" << endl;
    swapByAddress(&x, &y);  // 传递地址, 使用 & 取地址
    cout << "  函数调用后: x=" << x << ", y=" << y << endl;
    // x 和 y 已经交换! x=20, y=10

    // 再次交换回来, 恢复原始值
    swapByAddress(&x, &y);

    // 1.3 引用传递: 可以交换原值
    cout << "\n--- 引用传递 ---" << endl;
    swapByReference(x, y);  // 直接传递变量, 引用自动绑定
    cout << "  函数调用后: x=" << x << ", y=" << y << endl;
    // x 和 y 已经交换! x=20, y=10

    // ----------------------------------------------------------
    // 2. 三种方式对原值的影响对比
    // ----------------------------------------------------------
    cout << "\n===== 修改原值的对比 =====" << endl;

    int num = 10;
    cout << "\n原始值: num=" << num << endl;

    // 2.1 值传递 - 不修改原值
    cout << "\n--- 值传递 ---" << endl;
    doubleValue(num);
    cout << "  函数调用后 num = " << num << endl;  // num 仍然是 10

    // 2.2 地址传递 - 修改原值
    cout << "\n--- 地址传递 ---" << endl;
    doubleAddress(&num);
    cout << "  函数调用后 num = " << num << endl;  // num 变成 20

    // 2.3 引用传递 - 修改原值
    cout << "\n--- 引用传递 ---" << endl;
    doubleReference(num);
    cout << "  函数调用后 num = " << num << endl;  // num 变成 40

    // ----------------------------------------------------------
    // 3. 数组的传递 (本质是地址传递)
    // ----------------------------------------------------------
    cout << "\n===== 数组传递 =====" << endl;

    int arr[] = {1, 2, 3, 4, 5};
    int size = 5;

    cout << "\n原始数组:" << endl;
    printArray(arr, size);

    // 数组传递是地址传递, 函数可以修改原数组
    modifyArray(arr, size);
    cout << "\n翻倍后的数组:" << endl;
    printArray(arr, size);
    // 输出: 2 4 6 8 10

    // ----------------------------------------------------------
    // 4. 总结表格
    // ----------------------------------------------------------
    cout << "\n===== 三种传递方式总结 =====" << endl;
    cout << "┌──────────┬──────────┬──────────┬──────────┐" << endl;
    cout << "│   方式   │  语法    │ 修改原值 │  说明    │" << endl;
    cout << "├──────────┼──────────┼──────────┼──────────┤" << endl;
    cout << "│ 值传递   │ func(int)│   否     │ 复制副本 │" << endl;
    cout << "│ 地址传递 │ func(int*)│   是    │ 传指针   │" << endl;
    cout << "│ 引用传递 │ func(int&)│   是    │ 传别名   │" << endl;
    cout << "└──────────┴──────────┴──────────┴──────────┘" << endl;

    return 0;
}

/*
 * ============================================================
 * 常见错误:
 * ============================================================
 * 1. 忘记值传递不能修改原值, 期望 swapByValue 能交换变量
 * 2. 地址传递时忘记用 & 取地址: swapByAddress(x, y) 应为 swapByAddress(&x, &y)
 * 3. 地址传递时忘记解引用: 直接用 a 而不是 *a
 * 4. 混淆 & 在声明中的含义:
 *    - int& a = x;  → 引用声明, a 是 x 的别名
 *    - &x           → 取地址运算符, 获取 x 的地址
 * ============================================================
 */
