#include <iostream>
using namespace std;

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 什么是指针
    // ----------------------------------------------------------
    /*
     * 指针 (Pointer) 是一种特殊的变量, 它存储的不是普通的值,
     * 而是另一个变量的内存地址。
     *
     * 内存地址: 每个变量在内存中都有一个唯一的地址
     * 指针: 存储这个地址的变量
     *
     * 类比: 变量是一个房间, 指针是房间的门牌号
     *       通过门牌号可以找到对应的房间
     */

    cout << "===== 1. 什么是指针 =====" << endl;

    int num = 42;
    int* ptr = &num;  // ptr 存储 num 的地址

    cout << "  num 的值: " << num << endl;
    cout << "  num 的地址: " << &num << endl;
    cout << "  ptr 的值 (num的地址): " << ptr << endl;
    cout << "  ptr 指向的值 (*ptr): " << *ptr << endl;

    // ----------------------------------------------------------
    // 2. & 取地址运算符
    // ----------------------------------------------------------
    cout << "\n===== 2. & 取地址运算符 =====" << endl;

    int a = 10;
    double b = 3.14;
    char c = 'A';

    // & 运算符获取变量的内存地址
    cout << "  int a = " << a << ", 地址: " << &a << endl;
    cout << "  double b = " << b << ", 地址: " << &b << endl;
    cout << "  char c = " << c << ", 地址: " << (void*)&c << endl;
    // 注意: 直接打印 char* 会被当作字符串, 需要 (void*) 转换

    // ----------------------------------------------------------
    // 3. * 解引用运算符
    // ----------------------------------------------------------
    cout << "\n===== 3. * 解引用运算符 =====" << endl;

    int value = 100;
    int* p = &value;  // p 指向 value

    cout << "  value = " << value << endl;
    cout << "  p = " << p << " (value的地址)" << endl;
    cout << "  *p = " << *p << " (通过指针访问value的值)" << endl;

    // 通过指针修改值
    *p = 200;  // 等价于 value = 200
    cout << "  修改 *p = 200 后:" << endl;
    cout << "  value = " << value << endl;
    cout << "  *p = " << *p << endl;

    // ----------------------------------------------------------
    // 4. 指针的声明与初始化
    // ----------------------------------------------------------
    cout << "\n===== 4. 指针的声明与初始化 =====" << endl;

    // 声明指针: 类型* 指针名
    int* ptr1;        // 声明一个指向 int 的指针 (未初始化, 危险!)
    double* ptr2;     // 声明一个指向 double 的指针
    char* ptr3;       // 声明一个指向 char 的指针

    // 初始化指针: 用 & 获取变量地址
    int x = 10;
    ptr1 = &x;  // 现在 ptr1 指向 x

    // 声明时直接初始化 (推荐!)
    int y = 20;
    int* ptr4 = &y;  // 声明时就初始化

    cout << "  ptr1 指向的值: " << *ptr1 << endl;
    cout << "  ptr4 指向的值: " << *ptr4 << endl;

    // 指针的类型必须与指向的变量类型匹配
    // int* ptr = &doubleVar;  // 错误! 类型不匹配

    // ----------------------------------------------------------
    // 5. nullptr (C++11)
    // ----------------------------------------------------------
    cout << "\n===== 5. nullptr (C++11) =====" << endl;

    // nullptr 是 C++11 引入的空指针常量
    // 表示指针不指向任何有效的内存地址
    int* nullPtr = nullptr;

    // 使用指针前必须检查是否为空
    if (nullPtr != nullptr) {
        cout << "  指针有效, 值为: " << *nullPtr << endl;
    } else {
        cout << "  指针为空, 不能解引用!" << endl;
    }

    // 旧写法 (不推荐):
    // int* oldNull = NULL;  // NULL 是宏, 值为 0
    // int* oldNull = 0;     // 直接赋值 0

    // nullptr 的优势:
    // 1. 类型安全: nullptr 是 std::nullptr_t 类型
    // 2. 语义清晰: 明确表示"空指针"
    // 3. 避免重载歧义

    // ----------------------------------------------------------
    // 6. sizeof(指针)
    // ----------------------------------------------------------
    cout << "\n===== 6. sizeof(指针) =====" << endl;

    // 在同一平台上, 所有类型的指针大小相同
    // 32位系统: 指针大小为 4 字节
    // 64位系统: 指针大小为 8 字节
    int* ip = nullptr;
    double* dp = nullptr;
    char* cp = nullptr;
    long long* lp = nullptr;

    cout << "  sizeof(int*)    = " << sizeof(ip) << " 字节" << endl;
    cout << "  sizeof(double*) = " << sizeof(dp) << " 字节" << endl;
    cout << "  sizeof(char*)   = " << sizeof(cp) << " 字节" << endl;
    cout << "  sizeof(long long*) = " << sizeof(lp) << " 字节" << endl;

    // 对比: 变量的大小与指针的大小不同
    int intVar = 0;
    double doubleVar = 0;
    cout << "\n  sizeof(int)    = " << sizeof(intVar) << " 字节" << endl;
    cout << "  sizeof(double) = " << sizeof(doubleVar) << " 字节" << endl;
    cout << "  sizeof(int*)   = " << sizeof(ip) << " 字节" << endl;
    cout << "  sizeof(double*) = " << sizeof(dp) << " 字节" << endl;
    cout << "  → 指针大小与类型无关, 只与系统位数有关" << endl;

    // ----------------------------------------------------------
    // 7. 指针的赋值与重新指向
    // ----------------------------------------------------------
    cout << "\n===== 7. 指针的赋值与重新指向 =====" << endl;

    int val1 = 10, val2 = 20;
    int* p1 = &val1;  // p1 指向 val1
    cout << "  p1 指向 val1, *p1 = " << *p1 << endl;

    p1 = &val2;  // p1 重新指向 val2
    cout << "  p1 重新指向 val2, *p1 = " << *p1 << endl;

    // 两个指针可以指向同一个变量
    int* p2 = p1;  // p2 也指向 val2
    cout << "  p2 = p1, *p2 = " << *p2 << endl;

    // 通过任一指针修改值, 另一个指针也能看到变化
    *p2 = 99;
    cout << "  修改 *p2 = 99 后:" << endl;
    cout << "  val2 = " << val2 << endl;
    cout << "  *p1 = " << *p1 << endl;
    cout << "  *p2 = " << *p2 << endl;

    // ----------------------------------------------------------
    // 8. 常见错误演示
    // ----------------------------------------------------------
    cout << "\n===== 8. 常见错误 =====" << endl;

    // 错误1: 使用未初始化的指针 (野指针)
    // int* wildPtr;          // 未初始化
    // *wildPtr = 10;         // 危险! 未定义行为

    // 错误2: 使用已释放的指针 (悬空指针)
    // int* danglingPtr = new int(10);
    // delete danglingPtr;
    // *danglingPtr = 20;     // 危险! 指针已失效

    // 错误3: 解引用空指针
    // int* nullP = nullptr;
    // *nullP = 10;           // 危险! 程序崩溃

    cout << "  以上错误已注释, 实际运行会导致程序崩溃!" << endl;
    cout << "  使用指针前必须:" << endl;
    cout << "    1. 确保指针已初始化" << endl;
    cout << "    2. 确保指针不为空" << endl;
    cout << "    3. 确保指针指向有效的内存" << endl;

    return 0;
}

/*
 * ============================================================
 * 总结:
 * ============================================================
 * 1. 指针是存储内存地址的变量
 * 2. & 运算符: 获取变量的地址
 * 3. * 运算符: 通过地址访问变量的值 (解引用)
 * 4. 声明: 类型* 指针名; (如 int* p;)
 * 5. 初始化: int* p = &变量; 或 int* p = nullptr;
 * 6. nullptr: C++11 的空指针常量, 推荐使用
 * 7. 指针大小: 32位系统4字节, 64位系统8字节
 * 8. 使用前必须检查: 是否初始化、是否为空、是否有效
 *
 * 指针的重要性:
 * - 动态内存管理
 * - 函数参数传递 (地址传递)
 * - 数据结构 (链表、树、图)
 * - 多态和虚函数
 * - 高效的大数据传递
 * ============================================================
 */
