#include <iostream>
using namespace std;

// ============================================================
// 一、什么是运算符重载
// ============================================================
// 运算符重载是指赋予已有的运算符新的功能,使其能够用于自定义数据类型(如类)
// 本质上,运算符重载是通过函数实现的,编译器将运算符转换为对应的函数调用
// 例如: a + b  可以被编译器解释为  a.operator+(b) 或 operator+(a, b)
// ============================================================

// 定义一个简单的整数包装类,用于演示运算符重载
class MyInt {
public:
    int value;

    // 构造函数
    MyInt(int v = 0) : value(v) {}

    // ============================================================
    // 二、成员函数方式重载运算符
    // ============================================================
    // 语法: 返回类型 operator运算符(参数列表) { 函数体 }
    // 成员函数方式中,左操作数是当前对象(*this),右操作数是参数
    // ============================================================

    // 重载 + 运算符(成员函数方式)
    // 左操作数是 *this, 右操作数是 other
    MyInt operator+(const MyInt& other) const {
        return MyInt(this->value + other.value);
    }

    // 重载 - 运算符(成员函数方式)
    MyInt operator-(const MyInt& other) const {
        return MyInt(this->value - other.value);
    }

    // 重载 == 运算符,用于比较
    bool operator==(const MyInt& other) const {
        return this->value == other.value;
    }

    // 重载前置 ++ 运算符
    MyInt& operator++() {
        ++this->value;
        return *this;  // 返回引用,支持链式操作
    }

    // 重载后置 ++ 运算符(int参数是占位符,用于区分前置和后置)
    MyInt operator++(int) {
        MyInt temp = *this;  // 保存当前值
        ++this->value;
        return temp;  // 返回旧值(值返回,不是引用)
    }

    // 重载 << 输出运算符(成员函数方式,不推荐)
    // 注意:这种方式要求左操作数是对象,用法为 obj << cout,不符合习惯
    // 所以 << 通常用友元函数方式重载

    // 声明友元函数,以便访问私有成员(如果value是private的话)
    friend ostream& operator<<(ostream& os, const MyInt& obj);
    friend MyInt operator*(const MyInt& a, const MyInt& b);
};

// ============================================================
// 三、友元函数方式重载运算符
// ============================================================
// 友元函数不是类的成员函数,但可以访问类的私有成员
// 友元函数方式中,所有操作数都通过参数传递
// 适用于左操作数不是当前类对象的情况(如 << 和 >>)
// ============================================================

// 重载 << 运算符(友元函数方式,推荐)
// 左操作数是 ostream,右操作数是自定义类
// 返回 ostream 引用以支持链式输出: cout << a << b << endl;
ostream& operator<<(ostream& os, const MyInt& obj) {
    os << obj.value;
    return os;
}

// 重载 * 运算符(友元函数方式)
// 友元函数可以访问类的私有成员
MyInt operator*(const MyInt& a, const MyInt& b) {
    return MyInt(a.value * b.value);
}

// ============================================================
// 四、可以重载和不可以重载的运算符
// ============================================================
// 【可以重载的运算符】
//   算术: +  -  *  /  %
//   关系: ==  !=  <  >  <=  >=
//   逻辑: &&  ||  !
//   位运算: &  |  ^  ~  <<  >>
//   赋值: =  +=  -=  *=  /=  %=  &=  |=  ^=  <<=  >>=
//   自增自减: ++  --
//   其他: []  ()  ->  ->*  new  delete  ,  (逗号)
//
// 【不可以重载的运算符】
//   .       (成员访问运算符)
//   .*      (成员指针访问运算符)
//   ::      (作用域解析运算符)
//   ?:      (条件运算符/三目运算符)
//   sizeof  (大小运算符)
//   #       (预处理运算符)
// ============================================================

// ============================================================
// 五、重载的限制和约定
// ============================================================
// 1. 不能创建新的运算符,只能重载已有的运算符
// 2. 不能改变运算符的优先级和结合性
// 3. 不能改变运算符的操作数个数(元数)
// 4. 至少有一个操作数是自定义类型(不能重载内置类型的运算)
// 5. 重载应保持运算符的原始语义(不要把 + 重载为减法)
// 6. 赋值运算符 = 必须作为成员函数重载
// 7. ()、[]、->、= 只能作为成员函数重载
// ============================================================

// 演示: 一个二维向量类,展示运算符重载的约定
class Vec2 {
public:
    double x, y;

    Vec2(double x = 0, double y = 0) : x(x), y(y) {}

    // 重载 + 保持加法语义(向量加法)
    Vec2 operator+(const Vec2& other) const {
        return Vec2(x + other.x, y + other.y);
    }

    // 重载 * 实现标量乘法(保持乘法语义)
    Vec2 operator*(double scalar) const {
        return Vec2(x * scalar, y * scalar);
    }

    // 重载 == 用于比较
    bool operator==(const Vec2& other) const {
        return (x == other.x) && (y == other.y);
    }

    // 重载 << 输出
    friend ostream& operator<<(ostream& os, const Vec2& v) {
        os << "(" << v.x << ", " << v.y << ")";
        return os;
    }
};

// ============================================================
// 六、常见错误示例(注释说明)
// ============================================================
// 错误1: 试图重载 . 运算符
//   void operator.() {}  // 编译错误! . 不可重载
//
// 错误2: 改变运算符的操作数个数
//   MyInt operator+(int a, int b, int c) {}  // 错误! + 只能有两个操作数
//
// 错误3: 重载内置类型的运算
//   int operator+(int a, int b) {}  // 错误! 不能重载内置类型的运算
//
// 错误4: 把 + 重载为减法(违反语义约定)
//   MyInt operator+(const MyInt& other) {
//       return MyInt(value - other.value);  // 虽然编译通过,但违反约定!
//   }
// ============================================================

int main() {
    cout << "===== 运算符重载概述演示 =====" << endl;

    // --- 成员函数方式重载的使用 ---
    cout << "\n--- 成员函数方式重载 ---" << endl;
    MyInt a(10), b(3);

    // 调用 a.operator+(b)
    MyInt c = a + b;
    cout << a << " + " << b << " = " << c << endl;   // 13

    // 调用 a.operator-(b)
    MyInt d = a - b;
    cout << a << " - " << b << " = " << d << endl;     // 7

    // 调用 a.operator==(b)
    cout << a << " == " << b << " : " << (a == b ? "true" : "false") << endl; // false

    // --- 友元函数方式重载的使用 ---
    cout << "\n--- 友元函数方式重载 ---" << endl;
    MyInt e = a * b;
    cout << a << " * " << b << " = " << e << endl;    // 30

    // --- 自增运算符 ---
    cout << "\n--- 自增运算符 ---" << endl;
    MyInt x(5);
    cout << "x = " << x << endl;              // 5
    cout << "++x = " << ++x << endl;          // 6 (前置,返回新值)
    cout << "x++ = " << x++ << endl;          // 6 (后置,返回旧值)
    cout << "x = " << x << endl;              // 7 (后置后值增加了)

    // --- Vec2 向量运算符重载 ---
    cout << "\n--- Vec2 向量运算符 ---" << endl;
    Vec2 v1(1.0, 2.0), v2(3.0, 4.0);
    Vec2 v3 = v1 + v2;
    cout << v1 << " + " << v2 << " = " << v3 << endl;   // (4, 6)

    Vec2 v4 = v1 * 2.0;
    cout << v1 << " * 2 = " << v4 << endl;               // (2, 4)

    cout << v1 << " == " << v2 << " : " << (v1 == v2 ? "true" : "false") << endl; // false

    // --- 总结 ---
    cout << "\n===== 总结 =====" << endl;
    cout << "1. 运算符重载本质是函数调用" << endl;
    cout << "2. 成员函数方式: 左操作数是 *this" << endl;
    cout << "3. 友元函数方式: 所有操作数都是参数" << endl;
    cout << "4. << 和 >> 通常用友元函数重载" << endl;
    cout << "5. = 、[]、()、-> 只能用成员函数重载" << endl;
    cout << "6. 重载应保持运算符的原始语义" << endl;

    return 0;
}
