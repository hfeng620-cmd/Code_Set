#include <iostream>
#include <string>
using namespace std;

// ============================================================
// 1. 无参数无返回值的函数
// ============================================================
// 适用场景: 打印固定内容、执行固定操作
void printLine() {
    cout << "================================" << endl;
}

void printWelcome() {
    printLine();
    cout << "  欢迎来到C++函数学习!" << endl;
    cout << "  本节学习函数的四种样式" << endl;
    printLine();
}

// ============================================================
// 2. 无参数有返回值的函数
// ============================================================
// 适用场景: 获取系统时间、获取固定配置、生成默认值
int getRandomNumber() {
    // 模拟返回一个"随机"数
    return 42;  // 经典的随机数 :)
}

string getDefaultName() {
    return "默认用户";
}

double getPi() {
    return 3.14159265358979;
}

// ============================================================
// 3. 有参数无返回值的函数
// ============================================================
// 适用场景: 打印信息、修改全局状态、执行操作
void printSeparator(char ch, int count) {
    for (int i = 0; i < count; i++) {
        cout << ch;
    }
    cout << endl;
}

void printName(string name, int times) {
    for (int i = 0; i < times; i++) {
        cout << "  " << name << endl;
    }
}

void printNumberInfo(int num) {
    cout << "  数字: " << num << endl;
    cout << "  奇偶: " << (num % 2 == 0 ? "偶数" : "奇数") << endl;
    cout << "  正负: " << (num > 0 ? "正数" : (num < 0 ? "负数" : "零")) << endl;
}

// ============================================================
// 4. 有参数有返回值的函数
// ============================================================
// 适用场景: 计算并返回结果、数据转换
int add(int a, int b) {
    return a + b;
}

int multiply(int a, int b) {
    return a * b;
}

double celsiusToFahrenheit(double celsius) {
    return celsius * 9.0 / 5.0 + 32.0;
}

double fahrenheitToCelsius(double fahrenheit) {
    return (fahrenheit - 32.0) * 5.0 / 9.0;
}

// 判断是否为素数
bool isPrime(int n) {
    if (n <= 1) return false;
    if (n <= 3) return true;
    if (n % 2 == 0 || n % 3 == 0) return false;
    for (int i = 5; i * i <= n; i += 6) {
        if (n % i == 0 || n % (i + 2) == 0)
            return false;
    }
    return true;
}

// ============================================================
// 5. 多参数函数
// ============================================================
// C++ 函数可以有多个参数, 用逗号分隔
// 参数可以是不同类型

// 计算矩形面积
double rectangleArea(double width, double height) {
    return width * height;
}

// 计算圆柱体体积
double cylinderVolume(double radius, double height) {
    return 3.14159265 * radius * radius * height;
}

// 格式化打印学生信息
void printStudentInfo(string name, int age, double score, string className) {
    printLine();
    cout << "  姓名: " << name << endl;
    cout << "  年龄: " << age << endl;
    cout << "  分数: " << score << endl;
    cout << "  班级: " << className << endl;
    printLine();
}

// 计算多个数的平均值 (使用数组参数)
double average(int arr[], int size) {
    if (size <= 0) return 0;
    int sum = 0;
    for (int i = 0; i < size; i++) {
        sum += arr[i];
    }
    return (double)sum / size;
}

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 无参数无返回值
    // ----------------------------------------------------------
    cout << "===== 1. 无参数无返回值 =====" << endl;
    printWelcome();  // 直接调用, 不传参, 不接收返回值

    // ----------------------------------------------------------
    // 2. 无参数有返回值
    // ----------------------------------------------------------
    cout << "\n===== 2. 无参数有返回值 =====" << endl;
    int randNum = getRandomNumber();  // 接收返回值
    cout << "  随机数: " << randNum << endl;
    cout << "  默认名: " << getDefaultName() << endl;
    cout << "  圆周率: " << getPi() << endl;

    // ----------------------------------------------------------
    // 3. 有参数无返回值
    // ----------------------------------------------------------
    cout << "\n===== 3. 有参数无返回值 =====" << endl;
    printSeparator('*', 30);
    printName("张三", 3);
    printSeparator('-', 30);
    printNumberInfo(42);

    // ----------------------------------------------------------
    // 4. 有参数有返回值
    // ----------------------------------------------------------
    cout << "\n===== 4. 有参数有返回值 =====" << endl;
    cout << "  3 + 5 = " << add(3, 5) << endl;
    cout << "  4 * 6 = " << multiply(4, 6) << endl;
    cout << "  100C = " << celsiusToFahrenheit(100) << "F" << endl;
    cout << "  212F = " << fahrenheitToCelsius(212) << "C" << endl;
    cout << "  17 是素数? " << (isPrime(17) ? "是" : "否") << endl;
    cout << "  18 是素数? " << (isPrime(18) ? "是" : "否") << endl;

    // ----------------------------------------------------------
    // 5. 多参数函数
    // ----------------------------------------------------------
    cout << "\n===== 5. 多参数函数 =====" << endl;
    cout << "  矩形面积 (5.0 x 3.0) = " << rectangleArea(5.0, 3.0) << endl;
    cout << "  圆柱体积 (r=2, h=5) = " << cylinderVolume(2.0, 5.0) << endl;

    printStudentInfo("李四", 20, 95.5, "计算机2班");

    int scores[] = {85, 90, 78, 92, 88};
    cout << "  平均分: " << average(scores, 5) << endl;

    // ----------------------------------------------------------
    // 6. 函数调用作为另一个函数的参数
    // ----------------------------------------------------------
    cout << "\n===== 6. 函数嵌套调用 =====" << endl;
    // 将 add 的返回值作为 multiply 的参数
    int result = multiply(add(2, 3), 4);  // (2+3) * 4 = 20
    cout << "  (2+3) * 4 = " << result << endl;

    // 将返回值直接用于打印
    printSeparator('=', add(10, 20));  // 打印 30 个 '='

    return 0;
}

/*
 * ============================================================
 * 总结:
 * ============================================================
 * | 样式               | 语法              | 适用场景         |
 * |--------------------|-------------------|------------------|
 * | 无参无返回值       | void func()       | 固定操作         |
 * | 无参有返回值       | int func()        | 获取默认值       |
 * | 有参无返回值       | void func(int)    | 打印/操作数据    |
 * | 有参有返回值       | int func(int)     | 计算并返回结果   |
 * | 多参数             | func(int,int,...) | 复杂计算         |
 * ============================================================
 */
