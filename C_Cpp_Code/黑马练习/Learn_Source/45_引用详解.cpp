// ===================== 4. 引用练习函数 =====================
void referenceDemo() {
    cout << "===== 引用基础 =====" << endl;

    // ---------- 引用的定义：给变量起别名 ----------
    int a = 10;
    int &ref = a;  // ref是a的别名，ref和a指向同一块内存

    cout << "a = " << a << endl;      // 输出: 10
    cout << "ref = " << ref << endl;  // 输出: 10

    // 通过引用修改原变量
    ref = 20;
    cout << "修改ref后, a = " << a << endl;  // 输出: 20

    // 通过原变量修改，引用也会变
    a = 30;
    cout << "修改a后, ref = " << ref << endl;  // 输出: 30

    // 验证：引用和原变量地址相同
    cout << "a的地址: " << &a << endl;
    cout << "ref的地址: " << &ref << endl;
    // 两个地址完全一样，说明引用只是别名，不占用新的内存空间（概念上）

    // ---------- 引用必须初始化 ----------
    // int &badRef;  // 错误！引用在声明时必须初始化
    // 原因：引用是别名，必须知道它是谁的别名

    // ---------- 引用一旦绑定，不可更改 ----------
    int b = 100;
    ref = b;  // 这不是让ref变为b的别名！这是把b的值赋给a（因为ref就是a）
    cout << "ref = b 之后, a = " << a << endl;  // 输出: 100
    cout << "ref = b 之后, ref = " << ref << endl;  // 输出: 100
    // ref仍然是a的别名，只是把b的值赋给了a

    cout << endl;
}

// ===================== 5. 引用与指针对比 =====================
void refVsPointer() {
    cout << "===== 引用与指针对比 =====" << endl;

    int x = 42;
    int y = 99;

    // 引用方式
    int &refX = x;   // refX是x的别名
    // 指针方式
    int *ptr = &x;   // ptr存储x的地址

    cout << "引用方式: refX = " << refX << endl;  // 42
    cout << "指针方式: *ptr = " << *ptr << endl;   // 42

    // 引用不能更换绑定目标
    // refX = y;  // 这是赋值，不是重新绑定！x变成了99
    refX = y;      // x的值变成99
    cout << "refX = y 后, x = " << x << endl;  // 99

    // 指针可以更换指向
    ptr = &y;      // ptr现在指向y
    cout << "ptr = &y 后, *ptr = " << *ptr << endl;  // 99

    // 引用的区别总结：
    // 1. 引用必须初始化，指针不必
    // 2. 引用不可更换绑定，指针可以
    // 3. 引用没有空引用（必须绑定合法对象），指针可以为NULL
    // 4. 引用更安全，指针更灵活
    // 5. sizeof(引用)是所绑定对象的大小，sizeof(指针)是地址的大小

    cout << endl;
}

// ===================== 6. 引用作为函数参数 =====================
void swapDemo() {
    cout << "===== 引用作为函数参数 =====" << endl;

    int m = 10, n = 20;
    cout << "交换前: m = " << m << ", n = " << n << endl;

    swapByRef(m, n);  // 传入的是m和n的引用（别名）
    cout << "交换后: m = " << m << ", n = " << n << endl;

    // 对比值传递：值传递会在函数内创建副本，不影响原变量
    // 引用传递：直接操作原变量，无拷贝开销，效率更高

    // const引用参数
    printByConstRef(m);  // 可以传入普通变量给const引用
    // 好处：不会意外修改传入的变量，且不需要拷贝

    cout << endl;
}

// ===================== 7. 引用作为返回值 =====================
void returnValueDemo() {
    cout << "===== 引用作为返回值 =====" << endl;

    int a = 10, b = 20;
    // getMax返回较大值的引用，可以直接修改它
    int &maxRef = getMax(a, b);  // maxRef是b的别名
    maxRef = 200;  // 修改maxRef就是修改b
    cout << "a = " << a << ", b = " << b << endl;  // a=10, b=200

    // 引用返回值可以作为左值（放在赋值号左边）
    getMax(a, b) = 500;  // 将较大值（b）设为500
    cout << "a = " << a << ", b = " << b << endl;  // a=10, b=500

    // 常见错误示例（注释说明，不实际运行）：
    // int& badReturn() {
    //     int local = 10;
    //     return local;  // 危险！返回局部变量引用
    // }
    // 局部变量在函数结束时销毁，返回的引用将指向已释放的内存
    // 这是未定义行为(UB)，可能导致程序崩溃或数据错误

    cout << endl;
}

// ===================== 8. 常量引用的特殊用法 =====================
void constRefSpecial() {
    cout << "===== 常量引用的特殊用法 =====" << endl;

    // 常量引用可以绑定到字面量（普通引用不行）
    const int &ref1 = 100;  // 合法！const引用可以绑定字面量
    // int &ref2 = 100;      // 非法！普通引用不能绑定字面量
    cout << "const引用绑定字面量: " << ref1 << endl;

    // const引用可以绑定到不同类型（会发生隐式转换）
    double d = 3.14;
    const int &ref3 = d;  // 合法！编译器创建临时变量
    // int &ref4 = d;      // 非法！类型不匹配
    cout << "const引用绑定double: " << ref3 << endl;  // 输出3（截断）

    // 解释：const int &ref3 = d 实际上等价于：
    // int temp = d;          // 创建临时变量
    // const int &ref3 = temp; // const引用绑定临时变量
    // 因为是const，所以不能通过ref3修改temp，这是安全的

    cout << endl;
}

// ===================== 主函数 =====================
int main() {
    cout << "========================================" << endl;
    cout << "       C++ 引用(reference)详解" << endl;
    cout << "========================================" << endl << endl;

    referenceDemo();    // 引用基础
    refVsPointer();     // 引用与指针对比
    swapDemo();         // 引用作为函数参数
    returnValueDemo();  // 引用作为返回值
    constRefSpecial();  // 常量引用特殊用法

    // 总结：
    // 1. 引用是变量的别名，必须初始化，不可重新绑定
    // 2. 引用作为函数参数：可以修改外部变量，效率高（无拷贝）
    // 3. 引用作为返回值：可以作为左值，但不能返回局部变量引用
    // 4. const引用：不能修改所绑定的变量，可以绑定字面量和临时量
    // 5. 引用比指针更安全、更简洁，是C++推荐的传参方式

    cout << "程序执行完毕！" << endl;
    return 0;
}
