#include <iostream>
using namespace std;

// ===================== 1. 引用作为函数参数 =====================
// 通过引用传递，可以在函数内部修改外部变量（类似指针传参，但语法更简洁）
void swapByRef(int &a, int &b) {
    // a和b是实参的别名，修改a/b就是修改原始变量
    int temp = a;
    a = b;
    b = temp;
    // 注意：这里不需要像指针那样解引用，直接使用即可
}

// ===================== 2. 常量引用 =====================
// const引用：不能通过该引用修改所绑定的变量
void printByConstRef(const int &val) {
    // val = 100;  // 错误！const引用不能修改值
    cout << "值为: " << val << endl;
    // const引用常用于函数参数，避免拷贝开销的同时保证不被修改
}

// ===================== 3. 引用作为返回值 =====================
// 返回静态变量的引用（安全，因为静态变量生命周期长）
int& getMax(int &a, int &b) {
    // 返回较大值的引用
    // 注意：绝对不要返回局部变量的引用！局部变量在函数结束后被销毁
    return (a > b) ? a : b;
}

