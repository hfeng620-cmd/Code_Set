#include <iostream>
#include <string>
using namespace std;

// ============================================================
// 1. 基本函数模板
// ============================================================
// template<typename T> 告诉编译器 T 是一个类型参数
// 编译器会根据调用时的实参类型自动推导 T 的具体类型

// 求两个值的最大值
template<typename T>
T maxValue(T a, T b) {
    return (a > b) ? a : b;
}

// 求两个值的最小值
template<typename T>
T minValue(T a, T b) {
    return (a < b) ? a : b;
}

// 交换两个值
template<typename T>
void mySwap(T& a, T& b) {
    T temp = a;
    a = b;
    b = temp;
}

// ============================================================
// 2. 多类型模板参数
// ============================================================
// 可以声明多个类型参数

// 两个不同类型的参数
template<typename T1, typename T2>
void printPair(T1 a, T2 b) {
    cout << "  (" << a << ", " << b << ")" << endl;
}

// 返回第一个类型, 接受两个不同类型
template<typename T1, typename T2>
T1 addDifferent(T1 a, T2 b) {
    return a + b;
}

// ============================================================
// 3. 模板与普通函数共存
// ============================================================
// 普通函数和模板函数可以同名, 普通函数优先匹配

// 普通函数版本
int maxValue(int a, int b) {
    cout << "  [普通函数] ";
    return (a > b) ? a : b;
}

// ============================================================
// 4. 模板函数的特化 (Specialization)
// ============================================================
// 为特定类型提供专门的实现

// 通用版本 (已在上面定义)

// 针对 const char* (C风格字符串) 的特化版本
// 因为默认的 > 比较的是指针地址, 不是字符串内容
template<>
const char* maxValue<const char*>(const char* a, const char* b) {
    cout << "  [特化版本] ";
    return (strcmp(a, b) > 0) ? a : b;
}

// ============================================================
// 5. 模板的实际应用: 通用数组操作
// ============================================================
// 打印数组
template<typename T>
void printArray(T arr[], int size) {
    cout << "  [";
    for (int i = 0; i < size; i++) {
        cout << arr[i];
        if (i < size - 1) cout << ", ";
    }
    cout << "]" << endl;
}

// 数组求和
template<typename T>
T arraySum(T arr[], int size) {
    T sum = T();  // 使用默认构造函数初始化
    for (int i = 0; i < size; i++) sum += arr[i];
    return sum;
}

// ============================================================
// 6. 模板的局限性说明
// ============================================================
/*
 * 模板的局限性:
 *
 * 1. 模板代码必须在编译时可见
 *    - 模板的声明和定义通常都放在头文件 (.h) 中
 *    - 因为编译器需要在编译时看到完整的模板定义才能实例化
 *    - 普通函数可以声明在 .h, 定义在 .cpp, 但模板不行
 *
 * 2. 模板的类型必须支持所用操作
 *    - 例如: maxValue 要求类型 T 支持 > 运算符
 *    - 如果传入不支持 > 的类型, 会编译错误
 *
 * 3. 模板错误信息复杂
 *    - 模板的编译错误通常很长, 难以阅读
 *    - C++20 的 concept 可以改善这个问题
 *
 * 4. 代码膨胀
 *    - 每种类型都会生成一份独立的代码
 *    - 可能增加编译时间和二进制文件大小
 */

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 基本模板使用 - 类型推导
    // ----------------------------------------------------------
    cout << "===== 1. 基本模板使用 =====" << endl;

    // 编译器根据实参类型自动推导 T
    cout << "  max(3, 5) = " << maxValue(3, 5) << endl;
    // T 被推导为 int

    cout << "  max(3.14, 2.86) = " << maxValue(3.14, 2.86) << endl;
    // T 被推导为 double

    cout << "  max('a', 'z') = " << maxValue('a', 'z') << endl;
    // T 被推导为 char

    cout << "  min(10, 20) = " << minValue(10, 20) << endl;

    // 注意: 传入普通函数 int 版本
    cout << "  max(10, 20) = " << maxValue(10, 20) << endl;
    // 调用普通函数版本 (精确匹配优先)

    // ----------------------------------------------------------
    // 2. 显式指定类型
    // ----------------------------------------------------------
    cout << "\n===== 2. 显式指定类型 =====" << endl;

    // 当编译器无法自动推导时, 需要显式指定
    cout << "  max<double>(3, 4.5) = " << maxValue<double>(3, 4.5) << endl;
    // 显式指定 T 为 double, int 3 被转换为 double

    // 当两个参数类型不同时, 需要显式指定
    // cout << "  max(3, 4.5) = " << maxValue(3, 4.5) << endl;
    // 错误! 编译器无法推导 T 是 int 还是 double

    // ----------------------------------------------------------
    // 3. 模板特化
    // ----------------------------------------------------------
    cout << "\n===== 3. 模板特化 =====" << endl;

    // 对于 C 风格字符串, 使用特化版本
    const char* s1 = "Hello";
    const char* s2 = "World";
    cout << "  max(\"Hello\", \"World\") = " << maxValue(s1, s2) << endl;
    // 调用特化版本, 按字典序比较

    // ----------------------------------------------------------
    // 4. 多类型模板参数
    // ----------------------------------------------------------
    cout << "\n===== 4. 多类型模板参数 =====" << endl;

    printPair(10, 3.14);           // T1=int, T2=double
    printPair("Hello", 42);        // T1=const char*, T2=int
    printPair(3.14, "World");      // T1=double, T2=const char*
    printPair(string("Hi"), 100);  // T1=string, T2=int

    // 不同类型的加法
    cout << "  addDifferent(3, 4.5) = " << addDifferent(3, 4.5) << endl;
    // T1=int, T2=double, 返回 int (3+4.5=7, 截断为 7)

    cout << "  addDifferent(3.5, 4) = " << addDifferent(3.5, 4) << endl;
    // T1=double, T2=int, 返回 double (3.5+4=7.5)

    // ----------------------------------------------------------
    // 5. 模板用于数组操作
    // ----------------------------------------------------------
    cout << "\n===== 5. 模板用于数组操作 =====" << endl;

    // 整数数组
    int intArr[] = {3, 1, 4, 1, 5, 9, 2, 6};
    int intSize = 8;
    cout << "  整数数组: ";
    printArray(intArr, intSize);
    cout << "  数组求和: " << arraySum(intArr, intSize) << endl;
    cout << "  数组最大值: " << arrayMax(intArr, intSize) << endl;

    // 浮点数组
    double doubleArr[] = {3.14, 2.71, 1.41, 1.73};
    cout << "\n  浮点数组: ";
    printArray(doubleArr, 4);
    cout << "  数组求和: " << arraySum(doubleArr, 4) << endl;

    // ----------------------------------------------------------
    // 6. swap 模板
    // ----------------------------------------------------------
    cout << "\n===== 6. swap 模板 =====" << endl;
    int x = 10, y = 20;
    cout << "  交换前: x=" << x << ", y=" << y << endl;
    mySwap(x, y);  // T 推导为 int
    cout << "  交换后: x=" << x << ", y=" << y << endl;

    return 0;
}

/*
 * 总结:
 * 1. template<typename T> 定义类型参数 T
 * 2. 编译器根据实参自动推导 T 的类型 (类型推导)
 * 3. 可以用 func<Type>(args) 显式指定类型
 * 4. 支持多个类型参数: template<typename T1, typename T2>
 * 5. 普通函数优先于模板函数 (精确匹配)
 * 6. 可以特化: template<> 为特定类型提供专门实现
 * 7. 模板的声明和定义通常都放在头文件中
 * 使用建议: 类型不重要时使用模板实现代码复用
 */
