// 5. 参数传递详解
// ============================================================

// 5.1 值传递（Pass by Value）
// 形参是实参的副本，修改形参不影响实参
void tryModify(int val) {
    val = 999;  // 只修改了副本
    cout << "函数内 val = " << val << endl;  // 999
}
// 调用后原值不变

// 5.2 引用传递（Pass by Reference）
// 形参是实参的别名，修改形参会修改实参
void modifyByRef(int& ref) {
    ref = 999;  // 修改了原始变量
    cout << "函数内 ref = " << ref << endl;  // 999
}
// 调用后原值被修改

// 5.3 指针传递（Pass by Pointer）
void modifyByPtr(int* ptr) {
    if (ptr != nullptr) {
        *ptr = 999;  // 通过指针修改原始变量
    }
}

// 5.4 const 引用传递（防止修改，避免拷贝）
void printValue(const int& val) {
    // val = 100;  // 编译错误！const 引用不可修改
    cout << "值为: " << val << endl;
}

// 5.5 多返回值（通过引用参数）
void getMinMax(int arr[], int size, int& minVal, int& maxVal) {
    minVal = arr[0];
    maxVal = arr[0];
    for (int i = 1; i < size; ++i) {
        if (arr[i] < minVal) minVal = arr[i];
        if (arr[i] > maxVal) maxVal = arr[i];
    }
}

