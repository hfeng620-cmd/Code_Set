// ============================================================
/*
 * 最佳实践:
 *   1. 单一职责：一个函数只做一件事
 *   2. 函数名要有描述性：如 calculateArea, isPrime, getMax
 *   3. 函数体不宜过长：超过 30 行考虑拆分
 *   4. 参数不宜过多：超过 4 个考虑用结构体
 *   5. 避免全局变量：用参数传递数据
 *   6. 使用 const 保护不应修改的参数
 *   7. 处理边界情况：空指针、空数组、除零等
 *   8. 使用 return early 减少嵌套
 *   9. 优先使用引用传递大对象（避免拷贝开销）
 *  10. 写函数注释：说明功能、参数、返回值
 */

