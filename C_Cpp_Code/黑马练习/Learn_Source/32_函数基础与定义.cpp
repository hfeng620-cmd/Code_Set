// 4. 函数调用机制（栈帧 Stack Frame）
// ============================================================
/*
 * 函数调用时，系统会在栈（Stack）上分配一块内存，称为"栈帧"。
 * 栈帧包含：
 *   1. 函数的参数（实参的副本）
 *   2. 函数的局部变量
 *   3. 返回地址（调用完成后回到哪里继续执行）
 *
 * 调用流程：
 *   main() 调用 add(10, 20)
 *   1. 在栈上为 add 创建新的栈帧
 *   2. 将实参 10, 20 复制到形参 a, b
 *   3. 执行 add 函数体
 *   4. return sum 将结果返回给 main
 *   5. 销毁 add 的栈帧，释放内存
 *   6. 回到 main 继续执行
 *
 * 栈帧的特点：
 *   - 每次函数调用都会创建一个新的栈帧
 *   - 函数返回时栈帧被销毁，局部变量随之消失
 *   - 栈空间有限（通常 1~8 MB），递归过深会导致栈溢出
 *   - 栈帧的创建和销毁由系统自动管理
 */

