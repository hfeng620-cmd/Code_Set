// 2. main 函数 - 程序入口
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 2.1 调用有参数有返回值的函数
    // ----------------------------------------------------------
    // 实参 (actual parameter): 调用时传入的实际值
    int x = 10, y = 20;

    // 调用 add 函数, 传入实参 x 和 y, 返回值赋给 result
    int result = add(x, y);
    cout << x << " + " << y << " = " << result << endl;
    // 输出: 10 + 20 = 30

    // ----------------------------------------------------------
    // 2.2 调用求最大值函数
    // ----------------------------------------------------------
    int a = 100, b = 200;
    cout << a << " 和 " << b << " 的最大值是: " << maxValue(a, b) << endl;
    // 输出: 100 和 200 的最大值是: 200

    // ----------------------------------------------------------
    // 2.3 调用无参数无返回值的函数
    // ----------------------------------------------------------
    sayHello();  // 直接调用, 不需要接收返回值

    // ----------------------------------------------------------
    // 2.4 调用有参数无返回值的函数
    // ----------------------------------------------------------
    printSum(30, 40);  // 输出: 30 + 40 = 70

    // ----------------------------------------------------------
    // 2.5 调用除法函数, 演示 return early
    // ----------------------------------------------------------
    double divResult = divide(10.0, 3.0);
    cout << "10.0 / 3.0 = " << divResult << endl;
    // 输出: 10.0 / 3.0 = 3.33333

    // 测试除零的情况 - 函数会提前返回
    double divZero = divide(10.0, 0.0);
    cout << "10.0 / 0.0 = " << divZero << endl;
    // 输出: 错误提示 + 返回 0

    // ----------------------------------------------------------
    // 2.6 形参与实参的区别演示
    // ----------------------------------------------------------
    cout << "\n--- 形参与实参的区别 ---" << endl;
    int num1 = 5;
    int num2 = 8;
    // num1 和 num2 是实参: 调用函数时传入的真实数据
    int sum = add(num1, num2);
    // 函数定义中的 a 和 b 是形参: 接收实参的副本
    cout << "实参 num1=" << num1 << ", num2=" << num2 << endl;
    cout << "形参 a, b 在函数内部使用, 函数结束后销毁" << endl;
    // 注意: 形参的修改不会影响实参（值传递）
    cout << endl;

    // ----------------------------------------------------------
    // 2.7 函数调用可以嵌套
    // ----------------------------------------------------------
    // 在表达式中直接使用函数的返回值
    cout << "maxValue(add(3,4), 10) = " << maxValue(add(3, 4), 10) << endl;
    // 先计算 add(3,4)=7, 再计算 maxValue(7,10)=10

    // 函数返回值可以直接用于其他操作
    cout << "add(5,3) * 2 = " << add(5, 3) * 2 << endl;

    // ----------------------------------------------------------
    // 2.8 函数重载演示
    // ----------------------------------------------------------
    cout << "\n--- 函数重载 ---" << endl;
    // 同一个函数名，不同参数类型，编译器自动选择
    cout << "absolute(-5) = " << absolute(-5) << endl;       // 调用 int 版本
    cout << "absolute(-3.14) = " << absolute(-3.14) << endl; // 调用 double 版本

    // ----------------------------------------------------------
    // 2.9 默认参数演示
    // ----------------------------------------------------------
    cout << "\n--- 默认参数 ---" << endl;
    greet("小明");                // 使用默认 greeting
    greet("小红", "早上好");      // 使用自定义 greeting

    // ----------------------------------------------------------
    // 2.10 辅助函数演示
    // ----------------------------------------------------------
    cout << "\n--- 辅助函数 ---" << endl;
    printLine(40, '-');
    cout << "这是一条分隔线演示" << endl;
    printLine(40, '-');

    // multiply 函数演示
    cout << "7 * 8 = " << multiply(7, 8) << endl;

    return 0;
}

