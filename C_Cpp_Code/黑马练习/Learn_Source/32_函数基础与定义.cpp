// 3. 函数定义 (Function Definition)
// ============================================================

// 3.1 有参数有返回值的函数
// 形参 (formal parameter): 函数定义中的参数, 用于接收实参的值
// 形参是实参的副本, 在函数内部使用, 函数结束后销毁
int add(int a, int b) {
    // a 和 b 是形参, 是实参的局部拷贝
    int sum = a + b;
    return sum;  // return 将结果返回给调用者
}

// 3.2 求最大值函数 - 使用三元运算符
int maxValue(int a, int b) {
    return (a > b) ? a : b;  // 一行搞定
}

// 3.3 无参数无返回值的函数
// void 表示函数不返回任何值
void sayHello() {
    cout << "Hello, 欢迎学习C++函数!" << endl;
    // void 函数可以省略 return, 或者写 return; 表示提前结束
}

// 3.4 有参数无返回值的函数
void printSum(int a, int b) {
    cout << a << " + " << b << " = " << (a + b) << endl;
    // 函数执行完毕后自动返回, 不需要 return 语句
}

// 3.5 演示 return early (提前返回)
// return early 常用于处理错误情况, 提前退出函数
double divide(double a, double b) {
    // 如果除数为0, 提前返回, 避免程序崩溃
    if (b == 0) {
        cout << "[错误] 除数不能为0!" << endl;
        return 0.0;  // 提前返回, 不执行后面的代码
    }
    // 只有 b != 0 时才会执行到这里
    return a / b;
}

// 3.6 函数重载（Overload）
// 同名函数，参数类型或个数不同，编译器自动选择
int absolute(int x) {
    return (x < 0) ? -x : x;
}

double absolute(double x) {
    return (x < 0.0) ? -x : x;
}

// 3.7 默认参数
// 默认参数必须从右向左连续
void greet(string name, string greeting) {
    cout << greeting << ", " << name << "!" << endl;
}

// 3.8 辅助函数
void printLine(int count, char ch) {
    for (int i = 0; i < count; ++i) cout << ch;
    cout << endl;
}

// 原型中省略了参数名，定义时必须补上
int multiply(int a, int b) {
    return a * b;
}

