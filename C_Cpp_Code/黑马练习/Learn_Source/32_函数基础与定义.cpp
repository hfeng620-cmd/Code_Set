// 9. 内联函数简介（inline）
// ============================================================
/*
 * 内联函数（inline function）：
 *   - 使用 inline 关键字建议编译器将函数调用替换为函数体
 *   - 目的：减少函数调用的开销（压栈、跳转、出栈）
 *   - 适用于：短小且频繁调用的函数
 *   - 注意：inline 只是建议，编译器可能忽略
 *
 * 示例：
 *   inline int square(int x) { return x * x; }
 *
 * 与宏的区别：
 *   - 内联函数有类型检查，宏没有
 *   - 内联函数有作用域，宏是全局文本替换
 *   - 内联函数可以调试，宏不能
 *
 * 现代编译器（C++11 起）会自动优化短小函数为内联
 * 通常不需要手动加 inline 关键字
 */

