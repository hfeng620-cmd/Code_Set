// 演示：参数传递的差异
void demonstrateParameterPassing() {
    cout << "\n===== 参数传递方式对比 =====" << endl;

    // 值传递
    int original = 42;
    cout << "值传递前: " << original << endl;
    tryModify(original);
    cout << "值传递后: " << original << endl;  // 42（不变）

    // 引用传递
    int refVal = 42;
    cout << "\n引用传递前: " << refVal << endl;
    modifyByRef(refVal);
    cout << "引用传递后: " << refVal << endl;  // 999（被修改）

    // 指针传递
    int ptrVal = 42;
    cout << "\n指针传递前: " << ptrVal << endl;
    modifyByPtr(&ptrVal);
    cout << "指针传递后: " << ptrVal << endl;  // 999（被修改）

    // const 引用
    cout << "\nconst 引用传递:" << endl;
    printValue(original);  // 只读，不可修改
}

// 演示：递归函数
void demonstrateRecursion() {
    cout << "\n===== 递归函数演示 =====" << endl;
    // 阶乘
    for (int i = 0; i <= 10; ++i) {
        cout << i << "! = " << factorial(i) << endl;
    }

    // 斐波那契
    cout << "\n斐波那契数列前 10 项: ";
    for (int i = 0; i < 10; ++i) {
        cout << fibonacci(i) << " ";
    }
    cout << endl;

    // 注意：递归过深会导致栈溢出
    // 例如：factorial(100000) 可能导致栈溢出
    cout << "警告：递归深度不宜过大，通常不超过几百层" << endl;
}

// 演示：实用函数
void demonstrateUtilityFunctions() {
    cout << "\n===== 实用函数演示 =====" << endl;

    // 素数判断
    cout << "20 以内的素数: ";
    for (int i = 2; i <= 20; ++i) {
        if (isPrime(i)) cout << i << " ";
    }
    cout << endl;

    // 字符串反转
    string str = "Hello, World!";
    cout << "反转前: " << str << endl;
    cout << "反转后: " << reverseStr(str) << endl;

    // 数组平均值
    int scores[] = {85, 92, 78, 96, 88, 73, 90};
    int size = sizeof(scores) / sizeof(scores[0]);
    cout << "平均分: " << average(scores, size) << endl;

    // 多返回值
    int minVal, maxVal;
    getMinMax(scores, size, minVal, maxVal);
    cout << "最低分: " << minVal << ", 最高分: " << maxVal << endl;
}

