/*
 * ============================================================
 *  转义字符详解
 * ============================================================
 *  转义字符（Escape Sequence）是以反斜杠 \ 开头的特殊字符序列，
 *  用于表示一些无法直接输入的控制字符或特殊字符。
 *
 *  常见转义字符一览：
 *    \n  —— 换行（newline）
 *    \t  —— 水平制表符（tab，通常占 4 或 8 个字符宽度）
 *    \\  —— 反斜杠字符本身
 *    \'  —— 单引号字符本身
 *    \"  —— 双引号字符本身
 *    \0  —— 空字符（null character），字符串结束标志
 *    \r  —— 回车（carriage return）
 *    \a  —— 响铃（alert / bell）
 *    \b  —— 退格（backspace）
 *    \f  —— 换页（form feed）
 *    \v  —— 垂直制表符（vertical tab）
 *    \?  —— 问号（用于避免三字母组问题，现代编译器一般不需要）
 *
 *  C++11 引入了原始字符串（Raw String），可以避免转义：
 *      R"(原始内容)"          —— 内容中的 \ 不会被当作转义
 *      R"分隔符(内容)分隔符"   —— 当内容中包含 )" 时使用自定义分隔符
 * ============================================================
 */

#include <iostream>
#include <string>   // 用于 std::string 和原始字符串
using namespace std;

int main() {
    // =========================================================
    // 一、常用转义字符演示
    // =========================================================
    cout << "===== 常用转义字符演示 =====" << endl;

    // \n —— 换行符，这是最常用的转义字符之一
    cout << "第一行\n第二行\n第三行" << endl;

    // \t —— 制表符，常用于对齐输出
    cout << "姓名\t年龄\t城市" << endl;
    cout << "张三\t25\t北京" << endl;
    cout << "李四\t30\t上海" << endl;
    cout << endl;

    // \\ —— 输出一个反斜杠
    // 因为 \ 本身就是转义的开始，所以要用 \\ 来表示一个真正的反斜杠
    cout << "文件路径: C:\\Users\\Desktop\\test.txt" << endl;

    // \" —— 输出一个双引号
    // 直接写 " 会导致编译器认为字符串结束，所以用 \" 转义
    cout << "他说：\"你好，世界！\"" << endl;

    // \' —— 输出一个单引号（在 char 中必须转义，在字符串中通常不需要）
    char ch = '\'';
    cout << "单引号字符: " << ch << endl;
    cout << endl;

    // =========================================================
    // 二、\0 空字符（字符串结束标志）
    // =========================================================
    cout << "===== \\0 空字符 =====" << endl;

    // C 风格字符串以 '\0' 结尾
    char str[] = "Hello";
    // 实际内存布局：'H' 'e' 'l' 'l' 'o' '\0'，共 6 个字节

    // sizeof 包含 '\0'，strlen 不包含 '\0'
    cout << "字符串: " << str << endl;
    cout << "sizeof(str): " << sizeof(str) << " (包含 '\\0')" << endl;

    // 如果手动在字符串中间放入 '\0'，cout 只会输出 '\0' 之前的部分
    char str2[] = {'H', 'i', '\0', 'B', 'y', 'e', '\0'};
    cout << "str2 输出: " << str2 << endl;       // 只输出 "Hi"
    cout << "str2 大小: " << sizeof(str2) << " 字节" << endl; // 7 个字节
    cout << endl;

    // =========================================================
    // 三、其他转义字符
    // =========================================================
    cout << "===== 其他转义字符 =====" << endl;

    // \a —— 响铃（可能在某些终端不会发出声音）
    // cout << "\a这可能会发出提示音" << endl;

    // \b —— 退格符，光标后退一格
    cout << "Hello\b\b\b\b\bWorld" << endl;
    // 输出效果：World（Hello 被后面的退格和 World 覆盖）

    // \r —— 回车符，光标回到行首（覆盖已有内容）
    // 注意：在不同操作系统中换行符不同：
    //   Windows: \r\n
    //   Linux/Mac: \n
    cout << "12345\rAB" << endl;
    // 输出效果：AB345（光标回到行首，AB 覆盖了 12）

    // \x 后跟十六进制数 —— 表示 ASCII 码对应的字符
    cout << "\\x41 = " << '\x41' << endl;  // 输出 'A'（A 的 ASCII 码是 65，即 0x41）

    // \ 后跟八进制数 —— 表示 ASCII 码对应的字符
    cout << "\\101 = " << '\101' << endl;   // 输出 'A'（八进制 101 = 十进制 65）
    cout << endl;

    // =========================================================
    // 四、\n vs endl 的区别
    // =========================================================
    cout << "===== \\n vs endl =====" << endl;

    // \n 只是输出一个换行字符，不会刷新缓冲区
    // endl 除了换行之外，还会强制刷新输出缓冲区
    // 区别：
    //   - \n  性能更好（适合大量输出时使用）
    //   - endl 性能稍差（每次都会刷新缓冲区）
    //   - 在调试时，如果程序崩溃，endl 能确保之前的输出已经显示
    //     而 \n 可能因为缓冲区未刷新而丢失输出

    cout << "使用 \\n 换行\n";
    cout << "使用 endl 换行" << endl;
    // 建议：在大量循环输出时用 '\n'，调试或需要即时显示时用 endl
    cout << endl;

    // =========================================================
    // 五、原始字符串 R"()"（C++11）
    // =========================================================
    cout << "===== 原始字符串 =====" << endl;

    // 普通字符串中需要大量转义时非常麻烦
    string normal = "C:\\Users\\Desktop\\file.txt\n包含\"引号\"和\\反斜杠";
    cout << "普通字符串:\n" << normal << endl;
    cout << endl;

    // 原始字符串中，\ 和 " 都是普通字符，不需要转义
    string raw = R"(C:\Users\Desktop\file.txt
包含"引号"和\反斜杠)";
    cout << "原始字符串:\n" << raw << endl;
    cout << endl;

    // 当内容中包含 )" 的组合时，可以使用自定义分隔符
    string custom = R"delim(这里包含 " 和 ) 组合)delim";
    cout << "自定义分隔符: " << custom << endl;

    // 原始字符串非常适合写正则表达式、文件路径、多行文本
    string regexPattern = R"(\d+\.\d+)";
    cout << "正则表达式: " << regexPattern << endl;
    cout << endl;

    // =========================================================
    // 六、常见错误
    // =========================================================
    cout << "===== 常见错误 =====" << endl;

    // 错误1：忘记转义反斜杠
    //   错误写法：cout << "C:\new\test.txt";  // \n 会被当成换行，\t 会被当成制表符
    //   正确写法：
    cout << "C:\\new\\test.txt" << endl;

    // 错误2：混淆单引号中的转义
    //   错误写法：char c = ''';  // 编译错误
    //   正确写法：
    char c = '\'';
    cout << "单引号字符: " << c << endl;

    // 错误3：以为 \0 后面的内容也会输出
    //   实际上 \0 是字符串终止符，后面的字符不会被 cout 输出
    //   （但它们确实存在于内存中）

    // 错误4：在原始字符串中忘记右括号和分隔符配对
    //   R"(内容"  —— 编译错误，缺少 )
    //   R"xxx(内容)xxx"  —— 正确

    cout << endl;
    cout << "===== 转义字符演示结束 =====" << endl;

    return 0;
}
