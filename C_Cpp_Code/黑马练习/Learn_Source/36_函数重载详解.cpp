#include <iostream>
#include <string>
using namespace std;

// ============================================================
// 1. 按参数类型重载
// ============================================================
// 函数名相同, 但参数类型不同

// 整数加法
int calculate(int a, int b) {
    cout << "  [int版本] ";
    return a + b;
}

// 浮点数加法
double calculate(double a, double b) {
    cout << "  [double版本] ";
    return a + b;
}

// 字符串拼接
string calculate(string a, string b) {
    cout << "  [string版本] ";
    return a + b;
}

// ============================================================
// 2. 按参数个数重载
// ============================================================
// 函数名相同, 但参数个数不同

// 求两个数的最大值
int maxValue(int a, int b) {
    return (a > b) ? a : b;
}

// 求三个数的最大值
int maxValue(int a, int b, int c) {
    return maxValue(maxValue(a, b), c);
}

// 求四个数的最大值
int maxValue(int a, int b, int c, int d) {
    return maxValue(maxValue(a, b), maxValue(c, d));
}

// ============================================================
// 3. 按参数顺序重载
// ============================================================
// 函数名相同, 参数类型相同, 但顺序不同

// 第一个参数是 int, 第二个是 double
void printInfo(int a, double b) {
    cout << "  [int, double] a=" << a << ", b=" << b << endl;
}

// 第一个参数是 double, 第二个是 int
void printInfo(double a, int b) {
    cout << "  [double, int] a=" << a << ", b=" << b << endl;
}

// ============================================================
// 4. 演示: 仅返回类型不同不能重载 (错误示例, 已注释)
// ============================================================
/*
// 以下代码会导致编译错误! 仅返回类型不同不能构成重载
int getResult(int a) {
    return a * 2;
}

double getResult(int a) {  // 错误: 只有返回类型不同
    return a * 2.0;
}
// 编译器无法区分这两个函数, 因为调用时只看参数列表
*/

// ============================================================
// 5. 综合重载示例: print 函数
// ============================================================
void print() {
    cout << "  [无参数] 空打印" << endl;
}

void print(int x) {
    cout << "  [int] " << x << endl;
}

void print(double x) {
    cout << "  [double] " << x << endl;
}

void print(string x) {
    cout << "  [string] " << x << endl;
}

void print(int x, int y) {
    cout << "  [int, int] " << x << ", " << y << endl;
}

void print(string x, int y) {
    cout << "  [string, int] " << x << ", " << y << endl;
}

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 按参数类型重载
    // ----------------------------------------------------------
    cout << "===== 1. 按参数类型重载 =====" << endl;

    // 编译器根据实参类型自动选择对应的函数版本
    cout << "calculate(3, 5) = " << calculate(3, 5) << endl;
    // 调用 int 版本, 输出: [int版本] 8

    cout << "calculate(3.14, 2.86) = " << calculate(3.14, 2.86) << endl;
    // 调用 double 版本, 输出: [double版本] 6.0

    cout << "calculate(\"Hello\", \"World\") = "
         << calculate(string("Hello"), string("World")) << endl;
    // 调用 string 版本, 输出: [string版本] HelloWorld

    // ----------------------------------------------------------
    // 2. 按参数个数重载
    // ----------------------------------------------------------
    cout << "\n===== 2. 按参数个数重载 =====" << endl;

    cout << "max(3, 5) = " << maxValue(3, 5) << endl;
    cout << "max(3, 5, 7) = " << maxValue(3, 5, 7) << endl;
    cout << "max(3, 5, 7, 2) = " << maxValue(3, 5, 7, 2) << endl;

    // ----------------------------------------------------------
    // 3. 按参数顺序重载
    // ----------------------------------------------------------
    cout << "\n===== 3. 按参数顺序重载 =====" << endl;

    printInfo(10, 3.14);    // 调用 [int, double] 版本
    printInfo(3.14, 10);    // 调用 [double, int] 版本

    // ----------------------------------------------------------
    // 4. 综合重载演示
    // ----------------------------------------------------------
    cout << "\n===== 4. 综合重载演示 =====" << endl;

    print();                    // 调用无参数版本
    print(42);                  // 调用 int 版本
    print(3.14);                // 调用 double 版本
    print(string("Hello"));     // 调用 string 版本
    print(10, 20);              // 调用 [int, int] 版本
    print(string("Hi"), 2024);  // 调用 [string, int] 版本

    // ----------------------------------------------------------
    // 5. 二义性调用 (ambiguous call) 错误演示
    // ----------------------------------------------------------
    cout << "\n===== 5. 二义性调用说明 =====" << endl;

    // 如果有以下两个函数:
    // void test(int a) { ... }
    // void test(long a) { ... }
    //
    // 调用 test(10) 时, 10 是 int, 会调用 int 版本 ✓
    // 但如果只有:
    // void test(float a) { ... }
    // void test(double a) { ... }
    //
    // 调用 test(3.14) 时, 3.14 默认是 double, 调用 double 版本 ✓
    // 调用 test(3.14f) 时, 3.14f 是 float, 调用 float 版本 ✓

    // 当编译器无法确定调用哪个版本时, 会报 ambiguous call 错误
    // 例如: 有 int 和 long 版本, 但调用时传入 short (需要隐式转换)
    // 编译器不知道该转换为 int 还是 long, 就会报错

    cout << "  二义性调用示例 (已注释):" << endl;
    cout << "  void func(int) 和 void func(float)" << endl;
    cout << "  调用 func(10) → 调用 int 版本" << endl;
    cout << "  调用 func(3.14) → 调用 float 版本 (需要隐式转换)" << endl;
    cout << "  但如果有 void func(int) 和 void func(long)" << endl;
    cout << "  调用 func(10) 可能产生二义性!" << endl;

    // ----------------------------------------------------------
    // 6. 重载的注意事项
    // ----------------------------------------------------------
    cout << "\n===== 6. 重载注意事项 =====" << endl;
    cout << "  1. 函数名必须相同" << endl;
    cout << "  2. 参数列表必须不同 (类型/个数/顺序)" << endl;
    cout << "  3. 仅返回类型不同不能重载" << endl;
    cout << "  4. 默认参数可能导致二义性" << endl;
    cout << "  5. const 修饰的参数可以构成重载" << endl;

    return 0;
}

/*
 * ============================================================
 * 常见错误:
 * ============================================================
 * 1. 仅返回类型不同就试图重载 → 编译错误
 * 2. 参数类型太接近导致二义性 (如 int/long/short)
 * 3. 默认参数与重载冲突 (详见 37_函数默认参数.cpp)
 * 4. 忘记函数重载只看参数列表, 不看返回类型
 *
 * 重载解析优先级:
 * 1. 精确匹配 (完全相同类型)
 * 2. 提升匹配 (char→int, float→double)
 * 3. 标准转换 (int→double, double→int)
 * 4. 用户自定义转换
 * 5. 可变参数匹配
 * 如果同一优先级有多个匹配, 则报二义性错误
 * ============================================================
 */
