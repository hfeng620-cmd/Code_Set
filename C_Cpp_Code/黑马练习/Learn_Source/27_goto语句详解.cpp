/*
 * ============================================================
 *  主题: goto 语句详解
 * ============================================================
 *  goto 语句可以无条件跳转到程序中的标签位置。它是 C 语言
 *  遗留下来的特性，在现代 C++ 中应尽量避免使用。本文件涵盖:
 *    1. goto 的基本语法和标签
 *    2. 为什么 goto 被认为是有害的
 *    3. goto 的合理使用场景
 *    4. 为什么 break/continue 更好
 *    5. 用 goto 实现的替代方案对比
 * ============================================================
 */

#include <iostream>
using namespace std;

int main()
{
    // ========================================
    // 1. goto 的基本语法
    // ========================================
    // 语法: goto 标签名;
    //        ...
    //        标签名: 语句;
    // 标签是一个标识符后面跟着冒号
    // goto 会无条件跳转到标签所在的位置

    cout << "--- goto 基本示例 ---" << endl;
    int i = 1;

loop_start:                         // 标签定义
    if (i > 5)
    {
        goto loop_end;              // 条件满足时跳转到 loop_end
    }
    cout << "i = " << i << endl;
    i++;
    goto loop_start;                // 跳回 loop_start 继续循环

loop_end:                           // 标签定义
    cout << "循环结束" << endl;

    // ========================================
    // 2. 为什么 goto 被认为是有害的
    // ========================================
    // Edsger Dijkstra 在 1968 年发表了著名的论文
    // "Go To Statement Considered Harmful"
    // goto 的问题:
    //   (1) 破坏程序的结构化，使代码难以理解
    //   (2) 跳转使得控制流难以跟踪
    //   (3) 难以维护和调试
    //   (4) 可能跳过变量初始化，导致未定义行为
    //   (5) 可以向后跳转，创建无法理解的"面条代码"

    // 滥用 goto 的反面示例 (不要这样写):
    // goto label2;         // 跳过 label1 的代码
    // label1:
    //     int x = 10;      // 如果从 label2 跳回来，x 未初始化
    // label2:
    //     cout << x;       // 未定义行为!

    // ========================================
    // 3. goto 的合理使用场景
    // ========================================
    // goto 唯一被广泛接受的用途: 从多层嵌套循环中跳出
    // 因为 break 只能跳出最内层循环

    cout << "--- goto 跳出多层循环 ---" << endl;
    // 在一个三维数组中查找特定值
    int target = 42;
    bool found = false;

    // 模拟三维数组搜索
    for (int x = 0; x < 3; x++)
    {
        for (int y = 0; y < 3; y++)
        {
            for (int z = 0; z < 3; z++)
            {
                // 模拟: 当 x=1, y=2, z=1 时找到目标
                int value = x * 10 + y * 3 + z;
                if (value == target)
                {
                    cout << "找到目标 " << target
                         << " 在位置 (" << x << "," << y << "," << z << ")" << endl;
                    found = true;
                    goto search_done;   // 直接跳出三层循环
                }
            }
        }
    }
search_done:
    if (!found)
    {
        cout << "未找到目标" << endl;
    }

    // ========================================
    // 4. 替代 goto 的方法
    // ========================================
    // 方法 1: 提取为函数 (推荐)
    // 方法 2: 使用标志变量
    // 方法 3: 使用 do { ... break; ... } while(0) 技巧

    // --- 方法 2: 标志变量 ---
    cout << "--- 替代方法: 标志变量 ---" << endl;
    bool shouldExit = false;
    for (int i = 0; i < 3 && !shouldExit; i++)
    {
        for (int j = 0; j < 3 && !shouldExit; j++)
        {
            for (int k = 0; k < 3; k++)
            {
                if (i == 1 && j == 1 && k == 1)
                {
                    cout << "标志变量法: 在 (1,1,1) 停止" << endl;
                    shouldExit = true;
                    break;              // 跳出最内层
                }
            }
        }
    }

    // --- 方法 3: do-while(0) 技巧 ---
    cout << "--- 替代方法: do-while(0) ---" << endl;
    do   // 注意: 这不是真正的循环，只是一个代码块，允许使用 break 跳出
    {
        for (int i = 0; i < 3; i++)
        {
            for (int j = 0; j < 3; j++)
            {
                if (i == 1 && j == 1)
                {
                    cout << "do-while(0) 法: 在 (1,1) 停止" << endl;
                    break;  // 跳出内层 for
                }
            }
            if (i >= 1)  // 需要额外条件才能跳出外层
            {
                break;
            }
        }
    } while (0);  // 条件为 false，只执行一次

    // ========================================
    // 5. 为什么 break/continue 更好
    // ========================================
    // break 和 continue 的优势:
    //   (1) 只能在循环内部使用，有明确的作用域
    //   (2) 不会跳转到任意位置，行为可预测
    //   (3) 不会跳过变量初始化
    //   (4) 代码结构清晰，易于理解
    //   (5) 编译器可以更好地优化

    cout << "--- break/continue 的结构化优势 ---" << endl;

    // 使用 break 和 continue 实现清晰的逻辑
    for (int num = 1; num <= 20; num++)
    {
        // 跳过偶数
        if (num % 2 == 0)
        {
            continue;   // 清晰: 跳过本次迭代
        }
        // 找到大于 15 的奇数就停止
        if (num > 15)
        {
            break;      // 清晰: 退出循环
        }
        cout << num << " ";
    }
    cout << endl;

    // ========================================
    // 6. goto 的向后跳转 (危险!)
    // ========================================
    // goto 可以向后跳转到之前的标签，这会导致:
    //   (1) 跳过的代码不会执行
    //   (2) 局部变量不会被重新初始化
    //   (3) 可能产生无限循环

    cout << "--- 向后跳转示例 (仅演示，不要滥用) ---" << endl;
    int counter = 0;

retry:                              // 向后跳转的目标
    counter++;
    cout << "尝试第 " << counter << " 次" << endl;

    if (counter < 3)
    {
        goto retry;                 // 向后跳转 (模拟重试逻辑)
    }
    cout << "重试完成" << endl;

    // 更好的做法: 使用循环
    cout << "--- 更好的做法: 使用循环 ---" << endl;
    for (int attempt = 1; attempt <= 3; attempt++)
    {
        cout << "尝试第 " << attempt << " 次" << endl;
    }
    cout << "重试完成" << endl;

    // ========================================
    // 7. 总结
    // ========================================
    // - 尽量避免使用 goto
    // - 使用 break, continue, return 替代
    // - 使用结构化的循环和条件语句
    // - 如果必须跳出多层嵌套，优先考虑提取函数
    // - goto 跳出多层嵌套是唯一可接受的用途

    cout << endl << "=== goto 语句详解完毕 ===" << endl;
    return 0;
}
