// ============================================================================
// 九、注释的常见错误
// ============================================================================

// 错误1: 多行注释没有正确关闭
// /* 这是一个没有关闭的注释
// 这行也会变成注释
// 直到遇到 */ 为止 -- 如果文件中没有 */, 则会导致编译错误

// 错误2: 多行注释嵌套 (C++ 不支持!)
// /* 外层注释 /* 内层注释 */ 这里已经是注释外面了 */
// 上面的例子中, 第一个 */ 就结束了注释, 后面的代码会暴露出来
// 正确做法: 使用 #if 0 ... #endif 来嵌套禁用代码

// 错误3: 注释与代码不同步
// int count = 10;  // 计数器初始值为 0  <-- 注释说 0, 代码是 10!
// 如果修改了代码, 必须同步更新注释, 否则注释会误导读者

// 错误4: 过度注释
// int a = 1;  // 定义变量 a, 赋值为 1
// int b = 2;  // 定义变量 b, 赋值为 2
// int c = a + b;  // 将 a 和 b 相加, 结果赋值给 c
// 这些注释完全多余, 代码本身已经很清楚了

// 错误5: 注释掉的代码长期保留
// 大量被注释掉的代码会让文件变得混乱
// 应该使用版本控制 (Git) 来管理历史代码, 而不是注释掉

