// ============================================================================
// 六、何时该注释, 何时代码应自文档化
// ============================================================================
// 原则: 好的代码应该自己解释自己, 注释是补充而非替代
//
// 应该注释的情况:
//   1. 解释"为什么"这样做, 而不是"做什么"
//   2. 复杂的算法或业务逻辑
//   3. 不直观的技巧或优化
//   4. 法律要求 (版权、许可证)
//   5. 公共 API 的文档
//
// 不需要注释的情况:
//   1. 明显的代码 (如 i++; // i 自增 1)
//   2. 好的命名可以代替注释 (如 getUserName() 不需要注释)
//   3. 简单的赋值和计算

// 不好的注释 -- 注释说的是"做什么", 而代码已经很明显:
int count = 0;     // 将 count 初始化为 0  <-- 多余的注释!

// 好的注释 -- 解释"为什么":
int retryCount = 0; // 初始化重试计数器, 网络不稳定时需要重试最多 3 次

// 用好的命名代替注释:
// 不好的写法:
int d;  // 距离到期的天数

// 好的写法 (不需要注释):
int daysUntilExpiration = 30;

