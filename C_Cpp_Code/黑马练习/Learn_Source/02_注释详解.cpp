//   @throws     -- 抛出的异常
//   @see        -- 参考其他函数/类
//   @note       -- 注意事项
//   @warning    -- 警告信息
//   @deprecated -- 已弃用的标记
//   @example    -- 使用示例
//   @file       -- 文件描述
//   @author     -- 作者
//   @date       -- 日期
//   @version    -- 版本号
//   @bug        -- 已知 bug
//   @todo       -- 待完成事项

/**
 * @brief 计算斐波那契数列的第 n 项
 *
 * 使用递归方法计算斐波那契数列
 * F(0) = 0, F(1) = 1
 * F(n) = F(n-1) + F(n-2), 当 n >= 2
 *
 * @param n  要计算的项数 (从 0 开始)
 * @return   斐波那契数列第 n 项的值
 *
 * @note     递归实现效率较低, n > 40 时会很慢
 * @warning  n 不能为负数, 否则行为未定义
 * @see      fibonacciIterative() -- 迭代版本, 效率更高
 *
 * @code
 *   int fib5 = fibonacci(5);   // fib5 = 5
 *   int fib10 = fibonacci(10); // fib10 = 55
 * @endcode
 *
 * @todo     添加迭代版本实现
 * @deprecated 建议使用 fibonacciIterative() 代替
 */
int fibonacci(int n) {
    if (n <= 0) return 0;
    if (n == 1) return 1;
    return fibonacci(n - 1) + fibonacci(n - 2);
}

