/*
 * ============================================================
 * 函数文档注释 (Function Documentation Comment)
 * ============================================================
 * 放在函数声明/定义之前, 说明函数的功能、参数、返回值
 *
 * @brief   计算两个整数的和
 *
 * @param   a  第一个加数
 * @param   b  第二个加数
 * @return  两个整数的和
 *
 * @pre     a 和 b 的和不超过 int 的范围
 * @post    返回 a + b 的结果
 *
 * @example
 *   int result = add(3, 5);  // result = 8
 *
 * @see     subtract(), multiply()
 * ============================================================
 */
int add(int a, int b) {
    return a + b;
}

/*
 * ============================================================
 * 计算圆的面积
 * ============================================================
 * @param radius  圆的半径, 必须为正数
 * @return 圆的面积 (double 类型)
 * @throws 无异常抛出
 * ============================================================
 */
double circleArea(double radius) {
    return 3.14159265358979 * radius * radius;
}

