// Doxygen 注释格式:
//   /** ... */  -- Java 风格 (推荐)
//   /// ...     -- C++ 风格 (单行)
//   /*! ... */  -- C 风格
//
// 常用 Doxygen 标签:
//   @brief      -- 简短描述 (一行)
//   @details    -- 详细描述 (可多行)
//   @param      -- 函数参数说明
