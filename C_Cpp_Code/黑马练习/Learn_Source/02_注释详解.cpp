/**
 * @brief 判断一个数是否为素数
 *
 * 使用试除法, 从 2 到 sqrt(n) 逐一检查是否能整除 n
 * 时间复杂度: O(sqrt(n))
 *
 * @param n  要判断的正整数
 * @return   如果 n 是素数返回 true, 否则返回 false
 *
 * @note     1 不是素数, 2 是最小的素数
 * @example
 *   bool result = isPrime(7);  // result = true
 *   bool result2 = isPrime(4); // result2 = false
 */
bool isPrime(int n) {
    // 小于 2 的数不是素数
    if (n < 2) return false;

    // 2 是素数
    if (n == 2) return true;

    // 偶数 (除了 2) 不是素数
    // 为什么单独判断偶数? 因为这样后面循环可以只检查奇数, 效率翻倍
    if (n % 2 == 0) return false;

    // 只检查奇数因子, 从 3 开始, 步长为 2
    // 为什么只检查到 sqrt(n)?
    // 因为如果 n 有因子 a, 则 n = a * b, 其中 a 和 b 一个 <= sqrt(n), 一个 >= sqrt(n)
    for (int i = 3; i * i <= n; i += 2) {
        if (n % i == 0) return false;  // 能整除, 不是素数
    }

    return true;  // 没有找到因子, 是素数
}

/**
 * @brief 计算两个数的最大公约数 (欧几里得算法)
 *
 * 使用辗转相除法:
 *   gcd(a, b) = gcd(b, a % b), 当 b != 0
 *   gcd(a, 0) = a
 *
 * @param a  第一个正整数
 * @param b  第二个正整数
 * @return   a 和 b 的最大公约数
 *
 * @see      lcm() -- 最小公倍数
 */
int gcd(int a, int b) {
    // 辗转相除法的核心: 不断用较大数除以较小数, 直到余数为 0
    while (b != 0) {
        int temp = b;
        b = a % b;  // 取余数
        a = temp;
    }
    return a;
}

