/*
 * ============================================================
 * 文件名: 35_函数声明与定义分离.cpp
 * 主题: C++ 函数声明与定义分离的最佳实践
 * ============================================================
 * 本文件涵盖:
 *   1. 函数声明 (原型) 的作用
 *   2. 函数定义的位置
 *   3. 前向声明 (Forward Declaration)
 *   4. 头文件与 #include guard 的概念
 *   5. 声明与定义分离的好处
 * ============================================================
 * 在实际项目中, 通常将函数声明放在 .h 头文件中,
 * 函数定义放在 .cpp 源文件中.
 * 本文件模拟这种分离方式, 全部写在一个文件中演示.
 * ============================================================
 */

#include <iostream>
#include <string>
using namespace std;

// ============================================================
// 1. 头文件模拟区: 放置函数声明 (原型)
// ============================================================
// 实际项目中, 这些声明会放在 .h 文件中
// 例如: math_utils.h

// 声明1: 计算两数之和
// 语法: 返回类型 函数名(参数类型列表);
// 注意: 声明中参数名可以省略, 但建议保留以提高可读性
int add(int a, int b);

// 声明2: 计算两数之积
int multiply(int a, int b);

// 声明3: 打印分隔线
void printLine(char ch = '=', int count = 40);

// 声明4: 求最大值
int maxOfThree(int a, int b, int c);

// 声明5: 计算阶乘
long long factorial(int n);

// ============================================================
// 2. 前向声明 (Forward Declaration)
// ============================================================
// 当函数A需要调用函数B, 而函数B定义在函数A之后时,
// 需要在函数A之前声明函数B

// 前向声明: 函数C 要调用函数D, 但函数D定义在后面
void functionD();  // 前向声明

void functionC() {
    cout << "  函数C被调用" << endl;
    functionD();  // 调用函数D (因为有前向声明, 编译器知道它的存在)
}

void functionD() {
    cout << "  函数D被调用" << endl;
    // functionD 不需要前向声明 functionC, 因为 functionC 定义在前面
}

// ============================================================
// 3. 头文件保护 (#include guard) 的概念演示
// ============================================================
/*
 * 在实际的头文件中, 需要使用 #include guard 防止重复包含:
 *
 * // math_utils.h
 * #ifndef MATH_UTILS_H    // 如果没有定义 MATH_UTILS_H
 * #define MATH_UTILS_H    // 定义 MATH_UTILS_H
 *
 * // 函数声明
 * int add(int a, int b);
 * int multiply(int a, int b);
 *
 * #endif  // 结束 #ifndef
 *
 * 或者使用 #pragma once (更简洁, 但不是标准):
 *
 * // math_utils.h
 * #pragma once
 * int add(int a, int b);
 * int multiply(int a, int b);
 */

// ============================================================
// main 函数
// ============================================================
int main() {

    // ----------------------------------------------------------
    // 1. 声明在前, 定义在后 - 函数正常调用
    // ----------------------------------------------------------
    cout << "===== 1. 声明与定义分离 =====" << endl;

    // add 和 multiply 在 main 之前只有声明, 定义在 main 之后
    // 但因为有声明, 编译器知道函数的签名, 可以正常调用
    int sum = add(10, 20);
    int product = multiply(6, 7);

    cout << "  10 + 20 = " << sum << endl;
    cout << "  6 * 7 = " << product << endl;

    // ----------------------------------------------------------
    // 2. 使用带默认参数的声明
    // ----------------------------------------------------------
    cout << "\n===== 2. 带默认参数的函数 =====" << endl;
    printLine();          // 使用默认参数: '=' x 40
    printLine('*', 20);   // 使用指定参数: '*' x 20
    printLine('-', 30);   // 使用指定参数: '-' x 30

    // ----------------------------------------------------------
    // 3. 多参数函数调用
    // ----------------------------------------------------------
    cout << "\n===== 3. 多参数函数 =====" << endl;
    cout << "  maxOfThree(10, 20, 15) = " << maxOfThree(10, 20, 15) << endl;
    cout << "  maxOfThree(100, 50, 80) = " << maxOfThree(100, 50, 80) << endl;

    // ----------------------------------------------------------
    // 4. 递归函数 (阶乘)
    // ----------------------------------------------------------
    cout << "\n===== 4. 递归函数 =====" << endl;
    for (int i = 0; i <= 10; i++) {
        cout << "  " << i << "! = " << factorial(i) << endl;
    }

    // ----------------------------------------------------------
    // 5. 前向声明的使用
    // ----------------------------------------------------------
    cout << "\n===== 5. 前向声明 =====" << endl;
    // functionC 调用 functionD, 但 functionD 定义在后面
    // 因为有前向声明, 编译器能找到 functionD
    functionC();

    // ----------------------------------------------------------
    // 6. 声明与定义分离的好处说明
    // ----------------------------------------------------------
    cout << "\n===== 6. 为什么要分离? =====" << endl;
    printLine();
    cout << "  1. 提高编译速度: 头文件只需编译一次" << endl;
    cout << "  2. 隐藏实现: 只暴露声明, 隐藏定义" << endl;
    cout << "  3. 代码组织: 多个 .cpp 可以共享声明" << endl;
    cout << "  4. 减少依赖: 修改实现不需要重新编译所有文件" << endl;
    printLine();

    return 0;
}

// ============================================================
// 函数定义区: 放置函数的具体实现
// ============================================================
// 实际项目中, 这些定义会放在对应的 .cpp 文件中
// 例如: math_utils.cpp
// #include "math_utils.h"  // 包含自己的头文件

// 定义1: 计算两数之和
// 声明中的参数名和定义中的参数名可以不同, 但类型必须一致
int add(int a, int b) {
    return a + b;
}

// 定义2: 计算两数之积
int multiply(int a, int b) {
    return a * b;
}

// 定义3: 打印分隔线
// 默认参数只在声明中指定, 定义中不要重复指定!
void printLine(char ch, int count) {
    for (int i = 0; i < count; i++) {
        cout << ch;
    }
    cout << endl;
}

// 定义4: 求三个数的最大值
int maxOfThree(int a, int b, int c) {
    int maxVal = a;
    if (b > maxVal) maxVal = b;
    if (c > maxVal) maxVal = c;
    return maxVal;
}

// 定义5: 计算阶乘 (递归)
long long factorial(int n) {
    // 基准情况: 0! = 1, 1! = 1
    if (n <= 1) return 1;
    // 递归情况: n! = n * (n-1)!
    return n * factorial(n - 1);
}

