/*
 * ============================================================
 *  主题: while 循环详解
 * ============================================================
 *  循环结构让程序能够重复执行某段代码。本文件涵盖:
 *    1. while 循环语法和执行流程
 *    2. 循环计数器示例
 *    3. 无限循环及其危害
 *    4. do-while 循环语法
 *    5. while 与 do-while 的区别
 *    6. do-while 的适用场景
 * ============================================================
 */

#include <iostream>
using namespace std;

int main()
{
    // ========================================
    // 1. while 循环基本语法
    // ========================================
    // 语法: while (条件表达式) { 循环体; }
    // 执行流程:
    //   (1) 判断条件表达式
    //   (2) 如果为 true，执行循环体
    //   (3) 执行完循环体后，回到步骤 (1)
    //   (4) 如果条件为 false，跳出循环

    // 示例: 打印 1 到 5
    cout << "--- 打印 1 到 5 ---" << endl;
    int i = 1;           // 循环变量初始化
    while (i <= 5)       // 循环条件
    {
        cout << i << " ";
        i++;             // 循环变量更新 (递增)
    }
    cout << endl;

    // ========================================
    // 2. 循环计数器示例
    // ========================================

    // 示例: 计算 1 到 100 的累加和
    cout << "--- 计算 1+2+...+100 ---" << endl;
    int sum = 0;
    int count = 1;
    while (count <= 100)
    {
        sum += count;    // 累加
        count++;
    }
    cout << "1 到 100 的和 = " << sum << endl;

    // 示例: 计算阶乘 5! = 5*4*3*2*1
    cout << "--- 计算 5! ---" << endl;
    int n = 5;
    int factorial = 1;
    int k = 1;
    while (k <= n)
    {
        factorial *= k;  // 累乘
        k++;
    }
    cout << "5! = " << factorial << endl;

    // 示例: 找出 1 到 50 中所有能被 3 整除的数
    cout << "--- 1~50 中能被 3 整除的数 ---" << endl;
    int num = 1;
    int countDiv3 = 0;
    while (num <= 50)
    {
        if (num % 3 == 0)
        {
            cout << num << " ";
            countDiv3++;
        }
        num++;
    }
    cout << endl;
    cout << "共有 " << countDiv3 << " 个" << endl;

    // ========================================
    // 3. 无限循环及其危害
    // ========================================
    // 如果循环条件始终为 true，循环将永远不会结束
    // 这会导致程序卡死，占用大量 CPU 资源

    // 危险示例 (不要运行):
    // while (true)
    // {
    //     cout << "这会一直输出..." << endl;
    //     // 没有 break 或条件变化，死循环!
    // }

    // 常见错误: 循环变量忘记更新
    // int m = 1;
    // while (m <= 5)
    // {
    //     cout << m << endl;
    //     // 忘记 m++; 导致 m 永远是 1，死循环!
    // }

    // 安全的无限循环用法 (配合 break):
    cout << "--- 带 break 的循环 ---" << endl;
    int val = 1;
    while (true)
    {
        if (val > 5)
            break;       // 满足条件时跳出循环
        cout << val << " ";
        val++;
    }
    cout << endl;

    // ========================================
    // 4. do-while 循环语法
    // ========================================
    // 语法: do { 循环体; } while (条件表达式);
    // 注意: 末尾有分号 !
    // 执行流程:
    //   (1) 先执行循环体
    //   (2) 再判断条件表达式
    //   (3) 如果为 true，回到步骤 (1)
    //   (4) 如果为 false，跳出循环
    // 关键区别: 循环体至少执行一次!

    cout << "--- do-while 示例 ---" << endl;
    int j = 10;
    do
    {
        cout << "j = " << j << endl;  // 即使 j=10 不满足 j<=5，也会执行一次
        j++;
    } while (j <= 5);  // 条件为 false，循环结束
    // 输出: j = 10 (执行了一次，即使条件一开始就不满足)

    // ========================================
    // 5. while 与 do-while 的区别
    // ========================================
    // while: 先判断，后执行 (可能一次都不执行)
    // do-while: 先执行，后判断 (至少执行一次)

    cout << "--- while vs do-while 对比 ---" << endl;
    int testVal = 100;

    // while 循环: 条件一开始就不满足，循环体不执行
    cout << "while 循环:" << endl;
    while (testVal < 10)
    {
        cout << "  while 循环体执行了" << endl;  // 不会执行
        testVal++;
    }
    cout << "  while 循环结束" << endl;

    // do-while 循环: 先执行一次，再判断条件
    testVal = 100;  // 重置
    cout << "do-while 循环:" << endl;
    do
    {
        cout << "  do-while 循环体执行了" << endl;  // 执行一次
        testVal++;
    } while (testVal < 10);
    cout << "  do-while 循环结束" << endl;

    // ========================================
    // 6. do-while 的适用场景
    // ========================================
    // do-while 特别适合: 需要至少执行一次的场景
    // 例如: 输入验证、菜单显示

    // 模拟输入验证 (这里用变量模拟，实际可配合 cin)
    cout << "--- 模拟输入验证 ---" << endl;
    int userInput = 0;
    int attempts = 0;
    do
    {
        if (attempts > 0)
        {
            cout << "输入无效，请重新输入 (1-10): " << endl;
        }
        // 模拟用户输入 (第一次输入 0 无效，第二次输入 7 有效)
        userInput = (attempts == 0) ? 0 : 7;
        cout << "用户输入: " << userInput << endl;
        attempts++;
    } while (userInput < 1 || userInput > 10);  // 输入不在范围内就继续循环
    cout << "有效输入: " << userInput << endl;

    // ========================================
    // 7. 嵌套的 while 循环 (简介)
    // ========================================
    // while 循环可以嵌套使用
    cout << "--- 嵌套 while: 打印 3x3 矩阵 ---" << endl;
    int row = 0;
    while (row < 3)
    {
        int col = 0;
        while (col < 3)
        {
            cout << "(" << row << "," << col << ") ";
            col++;
        }
        cout << endl;
        row++;
    }

    cout << endl << "=== while 循环详解完毕 ===" << endl;
    return 0;
}
