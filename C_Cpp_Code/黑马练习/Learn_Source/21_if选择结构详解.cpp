/*
 * ============================================================
 *  主题: if 选择结构详解
 * ============================================================
 *  if 语句是 C++ 中最基本的条件判断结构，根据条件的真假
 *  来决定执行哪一段代码。本文件涵盖:
 *    1. 基本 if 语法
 *    2. if-else 双分支
 *    3. if-else if-else 多分支
 *    4. 单条语句 vs 代码块 {}
 *    5. 嵌套 if
 *    6. 常见错误: 误用 = 代替 ==
 *    7. 悬垂 else (dangling else) 问题
 * ============================================================
 */

#include <iostream>
using namespace std;

int main()
{
    // ========================================
    // 1. 基本 if 语法
    // ========================================
    // 语法: if (条件表达式) { 语句; }
    // 条件表达式为 true (非零) 时执行花括号内的代码
    int score = 85;

    if (score >= 60)
    {
        cout << "及格了! 分数: " << score << endl;
    }

    // ========================================
    // 2. if-else 双分支
    // ========================================
    // 当条件为 false 时，执行 else 分支
    int age = 16;

    if (age >= 18)
    {
        cout << "已成年，可以进入" << endl;
    }
    else
    {
        cout << "未成年，禁止进入" << endl;
    }

    // ========================================
    // 3. if-else if-else 多分支
    // ========================================
    // 可以有多个 else if 来处理多个条件
    // 条件从上到下依次判断，一旦某个条件为 true，执行对应分支后跳出整个 if 结构
    int grade = 78;

    if (grade >= 90)
    {
        cout << "优秀" << endl;
    }
    else if (grade >= 80)
    {
        cout << "良好" << endl;
    }
    else if (grade >= 70)
    {
        cout << "中等" << endl;       // 这个分支会被执行
    }
    else if (grade >= 60)
    {
        cout << "及格" << endl;
    }
    else
    {
        cout << "不及格" << endl;
    }

    // ========================================
    // 4. 单条语句 vs 代码块 {}
    // ========================================
    // 如果 if 后只有一条语句，可以省略花括号，但不推荐
    // 省略花括号容易在后续添加代码时引入 bug

    // 不推荐的写法 (省略花括号):
    int a = 10;
    if (a > 5)
        cout << "a 大于 5" << endl;   // 只有这一条语句受 if 控制

    // 推荐的写法 (始终使用花括号):
    if (a > 5)
    {
        cout << "a 大于 5" << endl;    // 明确的作用域，便于维护
    }

    // ========================================
    // 5. 嵌套 if
    // ========================================
    // if 语句内部可以再嵌套 if 语句
    // 注意: else 与最近的未匹配的 if 配对
    int num = 15;

    if (num > 0)
    {
        cout << num << " 是正数" << endl;
        if (num % 2 == 0)
        {
            cout << num << " 是偶数" << endl;
        }
        else
        {
            cout << num << " 是奇数" << endl;  // 输出这个
        }
    }
    else if (num < 0)
    {
        cout << num << " 是负数" << endl;
    }
    else
    {
        cout << "num 等于 0" << endl;
    }

    // ========================================
    // 6. 常见错误: 误用 = 代替 ==
    // ========================================
    // = 是赋值运算符，== 是比较运算符
    // 编译器通常会给出警告，但不会报错

    int x = 5;

    // 错误写法 (常犯错误):
    // if (x = 3)   // 这是赋值! x 变成 3，表达式值为 3 (非零，即 true)
    // {
    //     cout << "x 等于 3" << endl;  // 总是会执行，逻辑错误!
    // }

    // 正确写法:
    if (x == 3)
    {
        cout << "x 等于 3" << endl;
    }
    else
    {
        cout << "x 不等于 3, x = " << x << endl;  // 输出这个
    }

    // 防御性写法: 把常量写在左边
    // if (3 == x)  // 如果误写成 3 = x，编译器会报错
    // {
    //     cout << "x 等于 3" << endl;
    // }

    // ========================================
    // 7. 悬垂 else (dangling else) 问题
    // ========================================
    // 当有多个 if 和一个 else 时，else 总是与最近的未匹配的 if 配对
    // 这可能与程序员的意图不一致

    int m = 1, n = 2;

    // 下面的 else 与内层 if 配对，而非外层 if
    if (m > 0)
        if (n > 5)
            cout << "m > 0 且 n > 5" << endl;
        else  // 这个 else 配对的是 if (n > 5)，不是 if (m > 0)
            cout << "n <= 5 (但 m 可能 > 0 也可能 <= 0)" << endl;

    // 如果想让 else 与外层 if 配对，必须使用花括号:
    if (m > 0)
    {
        if (n > 5)
        {
            cout << "m > 0 且 n > 5" << endl;
        }
    }
    else  // 现在 else 明确与外层 if 配对
    {
        cout << "m <= 0" << endl;
    }

    // ========================================
    // 综合示例: 判断闰年
    // ========================================
    int year = 2024;
    // 闰年规则: 能被4整除但不能被100整除，或者能被400整除
    if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0))
    {
        cout << year << " 年是闰年" << endl;
    }
    else
    {
        cout << year << " 年不是闰年" << endl;
    }

    cout << endl << "=== if 选择结构详解完毕 ===" << endl;
    return 0;
}
