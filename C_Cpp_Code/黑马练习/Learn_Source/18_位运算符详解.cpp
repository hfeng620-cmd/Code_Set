/*
 * ============================================================
 *  位运算符详解（全面扩展版）
 * ============================================================
 *  位运算符直接操作整数的二进制位（bit），效率非常高。
 *    &   按位与：两位都为 1 才为 1
 *    |   按位或：有一位为 1 就为 1
 *    ^   按位异或：两位不同为 1，相同为 0
 *    ~   按位取反：0 变 1，1 变 0
 *    <<  左移：所有位向左移动，低位补 0（相当于乘 2^n）
 *    >>  右移：所有位向右移动（相当于除 2^n）
 *
 *  核心知识：
 *    1. 计算机中所有数据都以二进制形式存储
 *    2. 负数用补码表示：取反加一
 *    3. 位运算效率极高，常用于底层编程、嵌入式、网络协议
 *    4. 位运算不短路（与逻辑运算 && || 不同）
 * ============================================================
 */

#include <iostream>
#include <bitset>     // 用于以二进制形式输出
#include <string>
using namespace std;

// 辅助函数：以二进制形式打印整数（8位）
void printBin(const string& label, int value) {
    cout << label << " = " << value
         << "  (二进制: " << bitset<8>(value) << ")" << endl;
}

// 辅助函数：以二进制形式打印整数（32位）
void printBin32(const string& label, int value) {
    cout << label << " = " << value
         << "  (二进制: " << bitset<32>(value) << ")" << endl;
}

int main() {
    // =========================================================
    // 一、二进制数系统基础
    // =========================================================
    cout << "===== 二进制数系统 =====" << endl;
    // 十进制：每一位是 10 的幂次（个、十、百、千...）
    //   456 = 4*100 + 5*10 + 6*1 = 4*10^2 + 5*10^1 + 6*10^0
    // 二进制：每一位是 2 的幂次
    //   1011 = 1*8 + 0*4 + 1*2 + 1*1 = 1*2^3 + 0*2^2 + 1*2^1 + 1*2^0 = 11

    // 在 C++ 中表示二进制：0b 前缀（C++14）
    int bin = 0b1011;  // 二进制 1011 = 十进制 11
    int oct = 013;     // 八进制 13 = 十进制 11
    int hex = 0xB;     // 十六进制 B = 十进制 11
    cout << "0b1011 = " << bin << endl;
    cout << "013    = " << oct << endl;
    cout << "0xB    = " << hex << endl;

    // 常用二进制模式
    cout << "8位全1:  " << bitset<8>(0xFF) << " = " << 0xFF << endl;    // 255
    cout << "8位全0:  " << bitset<8>(0x00) << " = " << 0x00 << endl;    // 0
    cout << "最高位1: " << bitset<8>(0x80) << " = " << 0x80 << endl;    // 128
    cout << "最低位1: " << bitset<8>(0x01) << " = " << 0x01 << endl;    // 1
    cout << endl;

    // =========================================================
    // 二、补码（Two's Complement）—— 负数的表示
    // =========================================================
    cout << "===== 补码表示 =====" << endl;
    // 正数的补码 = 原码（直接转二进制）
    // 负数的补码 = 对应正数的二进制取反 + 1
    // 例如：-5 的补码
    //   5 的二进制:  00000101
    //   取反:        11111010
    //   加1:         11111011  → 这就是 -5 的补码

    int pos = 5;
    int neg = -5;
    printBin32("正数 5", pos);
    printBin32("负数 -5", neg);
    // -5 的二进制 = ~5 + 1 = 11111111111111111111111111111011
    cout << "~5 = " << (~pos) << endl;
    cout << "~5 + 1 = " << (~pos + 1) << endl;  // -5

    // 补码的性质：
    // 1. 最高位是符号位：0=正，1=负
    // 2. 0 的补码是全 0
    // 3. -1 的补码是全 1
    cout << "-1 的二进制: " << bitset<8>(-1) << " (全1)" << endl;

    // 无符号数没有补码概念，直接用原码
    unsigned int unsig = 0xFFFFFFFB;  // 和 -5 的补码相同
    cout << "unsigned 0xFFFFFFFB = " << unsig << endl;  // 4294967291
    cout << endl;

    // =========================================================
    // 三、按位与 &：两位都为 1 才为 1
    // =========================================================
    cout << "===== 按位与 & =====" << endl;
    int a = 12, b = 10;  // 12=00001100, 10=00001010
    printBin("a", a);
    printBin("b", b);
    printBin("a & b", a & b);  // 00001000 = 8

    // 应用1：判断奇偶数（比 % 更快）
    int num = 7;
    cout << num << " 是" << ((num & 1) ? "奇数" : "偶数") << endl;
    // 原理：奇数的最低位一定是 1

    // 应用2：用掩码提取指定位
    int val = 0b11010;
    int mask = 1 << 3;  // 掩码：第 3 位
    cout << "11010 的第 3 位是: " << ((val & mask) ? 1 : 0) << endl;

    // 应用3：清除特定位（配合 ~ 使用）
    int flags = 0b1111;
    flags &= ~(1 << 2);  // 清除第 2 位
    printBin("清除第2位后", flags);  // 00001011 = 11

    // 应用4：判断一个数是否为 2 的幂次方
    int testNum = 16;
    bool isPowerOf2 = (testNum > 0) && ((testNum & (testNum - 1)) == 0);
    cout << testNum << (isPowerOf2 ? " 是" : " 不是") << "2的幂次方" << endl;
    // 原理：2的幂次方只有1个1，减1后所有位取反，与运算结果为0
    //   16 = 10000, 15 = 01111, 16 & 15 = 0
    cout << endl;

    // =========================================================
    // 四、按位或 |：有一位为 1 就为 1
    // =========================================================
    cout << "===== 按位或 | =====" << endl;
    int c = 12, d = 10;
    printBin("c", c);
    printBin("d", d);
    printBin("c | d", c | d);  // 00001110 = 14

    // 应用1：设置某一位为 1
    int flags2 = 0;
    flags2 |= (1 << 2);  // 设置第 2 位
    flags2 |= (1 << 0);  // 设置第 0 位
    printBin("设置第0和第2位", flags2);  // 00000101 = 5

    // 应用2：合并标志位
    int readPerm = 0b001;
    int writePerm = 0b010;
    int execPerm = 0b100;
    int allPerms = readPerm | writePerm | execPerm;
    printBin("合并所有权限", allPerms);  // 00000111 = 7
    cout << endl;

    // =========================================================
    // 五、按位异或 ^：两位不同为 1，相同为 0
    // =========================================================
    cout << "===== 按位异或 ^ =====" << endl;
    int e = 12, f = 10;
    printBin("e", e);
    printBin("f", f);
    printBin("e ^ f", e ^ f);  // 00000110 = 6

    // 异或的重要性质：
    cout << "--- 异或性质 ---" << endl;
    cout << "12 ^ 12 = " << (12 ^ 12) << endl;  // 0（自身异或为0）
    cout << "12 ^ 0  = " << (12 ^ 0) << endl;   // 12（与0异或不变）
    cout << "12 ^ 0xFFFFFFFF = " << (12 ^ (int)0xFFFFFFFF) << endl;  // -13（等价于取反）
    // 异或满足交换律和结合律
    // a ^ b ^ a = b（自我消除性质）

    // 应用1：不用临时变量交换两个数（经典技巧）
    cout << "--- XOR 交换 ---" << endl;
    int x = 5, y = 9;
    cout << "交换前: x=" << x << ", y=" << y << endl;
    x ^= y;   // x = x ^ y
    y ^= x;   // y = y ^ (x ^ y) = x
    x ^= y;   // x = (x ^ y) ^ x = y
    cout << "交换后: x=" << x << ", y=" << y << endl;

    // 应用2：找出数组中唯一出现奇数次的元素
    cout << "--- 找唯一奇数次元素 ---" << endl;
    int arr[] = {2, 3, 2, 3, 4, 4, 5};
    int unique = 0;
    for (int i = 0; i < 7; ++i) unique ^= arr[i];
    cout << "数组中唯一出现奇数次的元素: " << unique << endl;  // 5
    // 原理：相同数异或为0，最终只剩下唯一的那个数

    // 应用3：简单加密/解密
    cout << "--- 简单加密 ---" << endl;
    char original = 'A';
    char key = 0x55;
    char encrypted = original ^ key;
    char decrypted = encrypted ^ key;
    cout << "原文: " << original << ", 密文: " << (int)encrypted
         << ", 解密: " << decrypted << endl;
    cout << endl;

    // =========================================================
    // 六、按位取反 ~：0 变 1，1 变 0
    // =========================================================
    cout << "===== 按位取反 ~ =====" << endl;
    int g = 12;
    printBin("g", g);
    cout << "~12 = " << (~g) << endl;  // -13
    // 规律：~x = -(x+1)
    cout << "~0 = " << (~0) << endl;    // -1
    cout << "~1 = " << (~1) << endl;    // -2
    cout << "~-1 = " << (~-1) << endl;  // 0

    // 应用：构造特定位数的掩码
    // 全 1 掩码（8位）
    unsigned char fullMask = ~0;  // 0xFF = 255
    cout << "8位全1掩码: " << bitset<8>(fullMask) << endl;

    // 清除某一位
    int fl = 0b1111;
    fl &= ~(1 << 2);  // 清除第 2 位
    printBin("清除第2位", fl);  // 00001011 = 11
    cout << endl;

    // =========================================================
    // 七、左移 <<：所有位向左移动，低位补 0
    // =========================================================
    cout << "===== 左移 << =====" << endl;
    // 左移 n 位 = 乘以 2^n
    int h = 3;
    printBin("h", h);
    printBin("h << 1", h << 1);  // 6  (3 * 2)
    printBin("h << 2", h << 2);  // 12 (3 * 4)
    printBin("h << 3", h << 3);  // 24 (3 * 8)
    cout << "3 * 8 = " << (3 * 8) << "  等价于  3 << 3 = " << (3 << 3) << endl;

    // 左移构建掩码（常用技巧）
    cout << "--- 左移构建掩码 ---" << endl;
    for (int i = 0; i < 8; ++i) {
        cout << "1 << " << i << " = " << bitset<8>(1 << i) << endl;
    }

    // 注意：左移量不能超过类型位数，否则是未定义行为
    // int 是 32 位：1 << 32 是未定义行为！
    cout << "警告：左移量必须小于类型位数（int=32）" << endl;
    cout << endl;

    // =========================================================
    // 八、右移 >>：所有位向右移动
    // =========================================================
    cout << "===== 右移 >> =====" << endl;
    // 右移 n 位 = 除以 2^n（向下取整）
    int i = 24;
    printBin("i", i);
    printBin("i >> 1", i >> 1);  // 12 (24 / 2)
    printBin("i >> 2", i >> 2);  // 6  (24 / 4)
    printBin("i >> 3", i >> 3);  // 3  (24 / 8)

    // 有符号负数右移：高位补符号位（算术右移）
    cout << "--- 算术右移（有符号）---" << endl;
    int negNum = -8;  // ...11111000
    printBin32("-8", negNum);
    printBin32("-8 >> 1", negNum >> 1);  // -4
    cout << "有符号 -8 >> 1 = " << (negNum >> 1) << endl;  // -4

    // 无符号数右移：高位补 0（逻辑右移）
    cout << "--- 逻辑右移（无符号）---" << endl;
    unsigned int u = 248;
    cout << "无符号 248 >> 1 = " << (u >> 1) << endl;  // 124

    // 注意：右移负数的行为是实现定义的（大多数实现用算术右移）
    cout << "警告：右移负数的行为是实现定义的" << endl;
    cout << endl;

    // =========================================================
    // 九、有符号 vs 无符号位运算
    // =========================================================
    cout << "===== 有符号 vs 无符号 =====" << endl;
    // 有符号数的最高位是符号位
    signed int si = -1;     // 二进制全 1
    unsigned int ui = 0xFFFFFFFF;  // 也是全 1
    cout << "signed   -1:           " << bitset<32>(si) << endl;
    cout << "unsigned 0xFFFFFFFF:   " << bitset<32>(ui) << endl;

    // 取反的行为
    cout << "~0 (signed):   " << (~0) << endl;          // -1
    cout << "~0u (unsigned): " << (~0u) << endl;        // 4294967295

    // 右移的区别
    cout << "-1 >> 1 (signed):   " << (-1 >> 1) << endl;    // -1（算术右移）
    cout << "0xFFFFFFFFu >> 1:   " << (0xFFFFFFFFu >> 1) << endl;  // 2147483647（逻辑右移）
    cout << endl;

    // =========================================================
    // 十、实际应用：权限管理系统
    // =========================================================
    cout << "===== 权限管理系统 =====" << endl;
    const int PERM_READ    = 1 << 0;  // 0001 = 1
    const int PERM_WRITE   = 1 << 1;  // 0010 = 2
    const int PERM_EXEC    = 1 << 2;  // 0100 = 4
    const int PERM_DELETE  = 1 << 3;  // 1000 = 8

    int userPerm = 0;
    cout << "初始权限: " << bitset<4>(userPerm) << endl;

    // 添加权限（用 |）
    userPerm |= PERM_READ;
    userPerm |= PERM_WRITE;
    cout << "添加读写权限: " << bitset<4>(userPerm) << endl;

    // 检查权限（用 &）
    cout << "有读权限? " << ((userPerm & PERM_READ) ? "是" : "否") << endl;
    cout << "有执行权限? " << ((userPerm & PERM_EXEC) ? "是" : "否") << endl;

    // 移除权限（用 & ~）
    userPerm &= ~PERM_WRITE;
    cout << "移除写权限: " << bitset<4>(userPerm) << endl;

    // 切换权限（用 ^）
    userPerm ^= PERM_EXEC;
    cout << "切换执行权限: " << bitset<4>(userPerm) << endl;
    userPerm ^= PERM_EXEC;
    cout << "再次切换执行权限: " << bitset<4>(userPerm) << endl;

    // 检查是否有多个权限
    if ((userPerm & (PERM_READ | PERM_WRITE)) == (PERM_READ | PERM_WRITE)) {
        cout << "同时拥有读写权限" << endl;
    }
    cout << endl;

    // =========================================================
    // 十一、提取特定位段
    // =========================================================
    cout << "===== 提取特定位段 =====" << endl;
    // 假设一个字节中：高4位是类型，低4位是值
    unsigned char data = 0b10110110;  // 类型=1011=11, 值=0110=6
    cout << "原始数据: " << bitset<8>(data) << endl;

    // 提取高4位（右移4位）
    unsigned char type = (data >> 4) & 0x0F;
    cout << "高4位(类型): " << bitset<4>(type) << " = " << (int)type << endl;

    // 提取低4位（与掩码）
    unsigned char value = data & 0x0F;
    cout << "低4位(值):   " << bitset<4>(value) << " = " << (int)value << endl;

    // 提取第2~5位（从0开始计数）
    int fullData = 0b11011010;  // 218
    int startPos = 2, bits = 4;
    int extracted = (fullData >> startPos) & ((1 << bits) - 1);
    cout << "提取第2~5位: " << bitset<4>(extracted) << " = " << extracted << endl;
    cout << endl;

    // =========================================================
    // 十二、位运算技巧汇总
    // =========================================================
    cout << "===== 位运算技巧 =====" << endl;

    // 技巧1：判断奇偶
    int n1 = 7;
    cout << n1 << (n1 & 1 ? " 是奇数" : " 是偶数") << endl;

    // 技巧2：乘除 2 的幂
    int n2 = 10;
    cout << n2 << " * 2 = " << (n2 << 1) << endl;
    cout << n2 << " / 2 = " << (n2 >> 1) << endl;

    // 技巧3：取绝对值（对负数取反加1）
    int n3 = -42;
    int abs_n3 = (n3 ^ (n3 >> 31)) - (n3 >> 31);
    cout << "|-42| = " << abs_n3 << endl;

    // 技巧4：不用分支求最大值/最小值
    int a1 = 10, b1 = 20;
    int diff = a1 - b1;
    int sign = (diff >> 31) & 1;  // 0 表示 a>=b，1 表示 a<b
    int maxVal = a1 - diff * sign;
    cout << "max(" << a1 << "," << b1 << ") = " << maxVal << endl;

    // 技巧5：统计二进制中 1 的个数（Brian Kernighan 算法）
    int bits = 0b11010110;  // 214
    int count = 0;
    int temp = bits;
    while (temp) {
        temp &= (temp - 1);  // 消除最低位的 1
        count++;
    }
    cout << bitset<8>(bits) << " 中有 " << count << " 个 1" << endl;

    // 技巧6：交换两个数（XOR）
    int sx = 42, sy = 99;
    sx ^= sy; sy ^= sx; sx ^= sy;
    cout << "XOR交换: sx=" << sx << ", sy=" << sy << endl;
    cout << endl;

    // =========================================================
    // 十三、常见错误
    // =========================================================
    cout << "===== 常见错误 =====" << endl;

    // 错误1：混淆 & 和 &&，| 和 ||
    //   & | 是位运算（不短路），&& || 是逻辑运算（短路）
    //   if (a & 1)  → 位运算，检查最低位
    //   if (a && 1) → 逻辑运算，检查 a 是否非零

    // 错误2：移位量 >= 类型位数是未定义行为
    //   int x = 1 << 32;  → 未定义行为！int 是 32 位
    //   正确：确保移位量 < 32（对于 int）

    // 错误3：~0 不是 1，而是 -1
    //   ~ 是按位取反，不是逻辑取反！
    //   ~0 = -1（所有位都变1），!0 = 1（逻辑取反）

    // 错误4：负数右移行为是实现定义的
    //   大多数实现用算术右移（补符号位），但不保证

    // 错误5：有符号数左移可能溢出
    //   1 << 31 对于 32 位 int 是未定义行为（溢出符号位）
    //   正确：用 1U << 31（unsigned）

    // 错误6：位运算优先级低于比较运算
    //   if (flags & mask == value)  → flags & (mask == value)
    //   正确：if ((flags & mask) == value)

    cout << endl;

    // =========================================================
    // 十四、练习题（TODO）
    // =========================================================
    /*
     * TODO 练习1：编写函数，判断一个整数是否为 2 的幂次方
     *   bool isPowerOfTwo(int n)
     *   提示：n > 0 && (n & (n-1)) == 0
     *
     * TODO 练习2：编写函数，统计一个整数的二进制表示中 1 的个数
     *   int countBits(int n)
     *   提示：使用 n & (n-1) 消除最低位的 1
     *
     * TODO 练习3：编写函数，不用临时变量交换两个整数
     *   void swapXOR(int& a, int& b)
     *   提示：使用三次异或操作
     *
     * TODO 练习4：编写函数，将整数的第 n 位设置为指定值（0 或 1）
     *   int setBit(int value, int pos, bool bitValue)
     *   提示：先清除该位，再设置新值
     *
     * TODO 练习5：实现一个简单的位图（bitmap）类
     *   支持：设置位、清除位、检查位、统计 1 的个数
     *   使用 unsigned int 数组作为底层存储
     */

    cout << "===== 位运算符演示结束 =====" << endl;
    return 0;
}

/*
 * ============================================================
 * 总结
 * ============================================================
 * 1. 位运算直接操作二进制位，效率极高
 * 2. 负数用补码表示：~x + 1 = -x
 * 3. & 按位与：提取/清除特定位；| 按位或：设置位
 * 4. ^ 异或：自我消除性质，用于交换、加密、找唯一元素
 * 5. ~ 取反：~x = -(x+1)
 * 6. 左移 << 等价于乘 2^n，右移 >> 等价于除 2^n
 * 7. 有符号右移补符号位（算术），无符号右移补 0（逻辑）
 * 8. 位运算优先级低于比较运算，务必加括号
 * 9. 移位量不能超过类型位数，否则是未定义行为
 * 10. 实际应用：权限管理、标志位、快速运算、加密
 * ============================================================
 */
