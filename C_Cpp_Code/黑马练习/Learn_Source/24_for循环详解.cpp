/*
 * ============================================================
 *  主题: for 循环详解
 * ============================================================
 *  for 循环是 C++ 中最常用的循环结构，特别适合已知循环
 *  次数的场景。本文件涵盖:
 *    1. for 循环语法: 初始化; 条件; 递增
 *    2. for 与 while 的等价关系
 *    3. for 循环中使用多个变量
 *    4. 嵌套 for 循环 (九九乘法表)
 *    5. 范围 for 循环 (range-based for) 简介
 * ============================================================
 */

#include <iostream>
using namespace std;

int main()
{
    // ========================================
    // 1. for 循环基本语法
    // ========================================
    // 语法: for (初始化; 条件表达式; 更新表达式) { 循环体; }
    // 执行流程:
    //   (1) 执行初始化 (仅一次)
    //   (2) 判断条件表达式
    //   (3) 如果为 true，执行循环体
    //   (4) 执行更新表达式
    //   (5) 回到步骤 (2)
    //   (6) 条件为 false 时，跳出循环

    // 示例: 打印 1 到 10
    cout << "--- 打印 1 到 10 ---" << endl;
    for (int i = 1; i <= 10; i++)
    {
        cout << i << " ";
    }
    cout << endl;

    // 解析:
    //   int i = 1;    -> 初始化: 声明循环变量 i 并赋初值 1
    //   i <= 10;      -> 条件: 当 i <= 10 时继续循环
    //   i++           -> 更新: 每次循环结束后 i 自增 1

    // ========================================
    // 2. for 与 while 的等价关系
    // ========================================
    // for (初始化; 条件; 更新) { 循环体; }
    // 等价于:
    //   初始化;
    //   while (条件) { 循环体; 更新; }

    cout << "--- for 与 while 等价 ---" << endl;

    // 使用 for 循环计算 1~100 的和
    int sumFor = 0;
    for (int i = 1; i <= 100; i++)
    {
        sumFor += i;
    }
    cout << "for 循环: 1+2+...+100 = " << sumFor << endl;

    // 使用 while 循环计算 (等价写法)
    int sumWhile = 0;
    int w = 1;           // 初始化
    while (w <= 100)     // 条件
    {
        sumWhile += w;
        w++;             // 更新
    }
    cout << "while 循环: 1+2+...+100 = " << sumWhile << endl;

    // ========================================
    // 3. for 循环的灵活用法
    // ========================================

    // (a) 递减循环: 从 10 到 1
    cout << "--- 递减循环 ---" << endl;
    for (int i = 10; i >= 1; i--)
    {
        cout << i << " ";
    }
    cout << endl;

    // (b) 步长不为 1: 打印偶数
    cout << "--- 步长为 2 ---" << endl;
    for (int i = 2; i <= 20; i += 2)
    {
        cout << i << " ";
    }
    cout << endl;

    // (c) 省略部分 (不推荐，但要知道语法)
    // for 的三个部分都可以省略
    int x = 0;
    for (; x < 5; )     // 省略初始化和更新 (等价于 while)
    {
        cout << x << " ";
        x++;
    }
    cout << endl;

    // ========================================
    // 4. for 循环中使用多个变量
    // ========================================
    // 初始化和更新部分可以用逗号分隔多个表达式

    cout << "--- 多变量 for 循环 ---" << endl;

    // 示例: 同时从两端向中间遍历
    int left = 0;
    int right = 10;
    for (int i = left, j = right; i <= j; i++, j--)
    {
        cout << "i=" << i << ", j=" << j << endl;
    }

    // 示例: 计算 1-2+3-4+5-...+99-100
    int alternatingSum = 0;
    for (int i = 1, sign = 1; i <= 100; i++, sign = -sign)
    {
        alternatingSum += sign * i;
    }
    cout << "1-2+3-4+...+99-100 = " << alternatingSum << endl;

    // ========================================
    // 5. 嵌套 for 循环
    // ========================================
    // for 循环内部可以再嵌套 for 循环
    // 外层循环控制行，内层循环控制列

    // (a) 打印矩形: 4 行 5 列的星号
    cout << "--- 4x5 矩形 ---" << endl;
    for (int row = 0; row < 4; row++)
    {
        for (int col = 0; col < 5; col++)
        {
            cout << "* ";
        }
        cout << endl;
    }

    // (b) 九九乘法表
    cout << "--- 九九乘法表 ---" << endl;
    for (int i = 1; i <= 9; i++)         // 外层: 行 (1~9)
    {
        for (int j = 1; j <= i; j++)     // 内层: 列 (1~i)
        {
            cout << j << "x" << i << "=" << i * j;
            cout << "\t";                // 用制表符对齐
        }
        cout << endl;
    }

    // ========================================
    // 6. 范围 for 循环 (range-based for)
    // ========================================
    // C++11 引入的新语法，用于遍历容器或数组中的每个元素
    // 语法: for (类型 变量 : 容器/数组) { 循环体; }

    cout << "--- 范围 for 循环 ---" << endl;

    // 遍历数组
    int arr[] = {10, 20, 30, 40, 50};
    for (int val : arr)
    {
        cout << val << " ";
    }
    cout << endl;

    // 使用 auto 自动推导类型
    for (auto val : arr)
    {
        cout << val << " ";
    }
    cout << endl;

    // 使用引用修改元素
    for (int& val : arr)
    {
        val *= 2;   // 每个元素乘以 2
    }
    for (int val : arr)
    {
        cout << val << " ";  // 输出: 20 40 60 80 100
    }
    cout << endl;

    // ========================================
    // 7. 常见错误
    // ========================================

    // 错误 1: 循环变量作用域问题
    // for (int i = 0; i < 5; i++) { ... }
    // cout << i;  // 错误! i 在 for 循环外部不可见

    // 错误 2: 在循环条件后加分号 (空循环体)
    // for (int i = 0; i < 5; i++);  // 分号结束了循环!
    // {
    //     cout << i;  // 这段代码只执行一次，不在循环内
    // }

    // 错误 3: 整数溢出
    // for (int i = 0; i < 3000000000; i++)  // 2^31 约 21 亿，可能溢出!
    // 应使用 long long 或 size_t

    cout << endl << "=== for 循环详解完毕 ===" << endl;
    return 0;
}
