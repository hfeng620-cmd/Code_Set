/*
 * ============================================================
 *  输入输出 cin / cout 详解
 * ============================================================
 *  C++ 使用流（stream）的方式进行输入输出：
 *    cout —— 控制台输出（Console Output），配合 << 插入运算符
 *    cin  —— 控制台输入（Console Input），配合 >> 提取运算符
 *
 *  头文件：
 *    #include <iostream>   —— 提供 cout、cin、endl 等
 *    #include <string>     —— 提供 string 类型的输入输出
 *    #include <limits>     —— 提供 numeric_limits（用于清除输入缓冲区）
 *
 *  基本用法：
 *    cout << "内容";       —— 输出内容到屏幕
 *    cin >> 变量;          —— 从键盘读取值存入变量
 *
 *  注意事项：
 *    1. cin >> 以空格、Tab、换行作为分隔符，不能读取含空格的字符串
 *    2. getline() 可以读取一整行（含空格）
 *    3. 输入类型不匹配时，cin 会进入"错误状态"
 * ============================================================
 */

#include <iostream>
#include <string>     // 用于 getline 和 string
#include <limits>     // 用于 numeric_limits
using namespace std;

int main() {
    // =========================================================
    // 一、cout 基本输出
    // =========================================================
    cout << "===== cout 基本输出 =====" << endl;

    // 使用 << 运算符可以连续输出多个内容
    cout << "Hello, World!" << endl;

    // 输出变量值
    int age = 25;
    double height = 1.75;
    string name = "张三";

    cout << "姓名: " << name << endl;
    cout << "年龄: " << age << endl;
    cout << "身高: " << height << " 米" << endl;

    // << 可以链式调用，连续输出多个内容
    cout << "姓名: " << name << ", 年龄: " << age << ", 身高: " << height << endl;
    cout << endl;

    // =========================================================
    // 二、cin 基本输入
    // =========================================================
    cout << "===== cin 基本输入 =====" << endl;

    // 读取单个变量
    int num;
    cout << "请输入一个整数: ";
    cin >> num;
    cout << "你输入的是: " << num << endl;

    // 一次读取多个变量（用空格或回车分隔）
    int a, b;
    cout << "请输入两个整数（用空格分隔）: ";
    cin >> a >> b;
    cout << "a = " << a << ", b = " << b << endl;
    cout << endl;

    // =========================================================
    // 三、cin 读取字符串的问题
    // =========================================================
    cout << "===== cin 读取字符串 =====" << endl;

    // cin >> string 会在遇到第一个空格时停止读取
    string word;
    cout << "请输入一个单词（cin >> 读取）: ";
    cin >> word;
    cout << "读取到: \"" << word << "\"" << endl;

    // 清除输入缓冲区中剩余的字符（包括换行符）
    // 这一步非常关键！否则后面的 getline 会读到残留内容
    cin.ignore(numeric_limits<streamsize>::max(), '\n');

    // =========================================================
    // 四、getline 读取含空格的整行
    // =========================================================
    cout << "===== getline 读取整行 =====" << endl;

    string line;
    cout << "请输入一整行文字（getline 读取）: ";
    getline(cin, line);
    cout << "读取到: \"" << line << "\"" << endl;

    // getline 的工作原理：
    //   读取输入流中的所有字符，直到遇到换行符 '\n' 为止
    //   换行符会被读取但不会存入字符串（被丢弃了）
    cout << endl;

    // =========================================================
    // 五、cin >> 与 getline 的对比
    // =========================================================
    cout << "===== cin >> vs getline 对比 =====" << endl;

    // 演示：cin >> 读取后缓冲区中残留的换行符
    string first, second;
    cout << "演示 cin >> 的残留问题：" << endl;
    cout << "请输入两个单词（用空格分隔）: ";
    cin >> first >> second;
    cout << "第一个单词: " << first << endl;
    cout << "第二个单词: " << second << endl;

    // 清除残留的换行符
    cin.ignore(numeric_limits<streamsize>::max(), '\n');

    string rest;
    cout << "现在用 getline 读取剩余输入: ";
    getline(cin, rest);
    cout << "剩余内容: \"" << rest << "\"" << endl;

    // 总结：
    //   cin >>   适合读取单个单词或数值（以空白字符为分隔符）
    //   getline  适合读取整行文本（包括空格）
    //   在 cin >> 之后使用 getline 前，一定要先清除缓冲区！
    cout << endl;

    // =========================================================
    // 六、输入错误处理
    // =========================================================
    cout << "===== 输入错误处理 =====" << endl;

    int value;
    cout << "请输入一个整数: ";
    cin >> value;

    // 如果用户输入的不是数字（比如输入了字母），cin 会进入错误状态
    // cin.fail() 返回 true 表示输入失败
    if (cin.fail()) {
        cout << "输入错误！你输入的不是一个有效的整数。" << endl;

        // 清除错误标志
        cin.clear();

        // 丢弃缓冲区中的无效输入
        cin.ignore(numeric_limits<streamsize>::max(), '\n');
    } else {
        cout << "你输入的整数是: " << value << endl;
    }
    cout << endl;

    // =========================================================
    // 七、格式化输出小技巧
    // =========================================================
    cout << "===== 格式化输出 =====" << endl;

    // 使用 \t 对齐
    cout << "产品\t\t价格\t数量" << endl;
    cout << "苹果\t\t5.5\t100" << endl;
    cout << "香蕉\t\t3.2\t200" << endl;

    // 使用 endl 和 \n 的区别（前面转义字符那节已详细讲解）
    // endl = 输出换行 + 刷新缓冲区
    // \n   = 只输出换行，不刷新缓冲区（性能更好）
    cout << endl;

    // 输出特殊字符
    cout << "输出双引号: \"" << endl;
    cout << "输出反斜杠: \\" << endl;
    cout << endl;

    // =========================================================
    // 八、常见错误总结
    // =========================================================
    cout << "===== 常见错误总结 =====" << endl;

    // 错误1：在 cin >> 之后直接用 getline，读到空行
    //   原因：cin >> 会把换行符留在缓冲区，getline 读到它就认为读完了
    //   解决：在 getline 前加 cin.ignore()

    // 错误2：输入类型不匹配
    //   例如：要求输入 int，用户输入了 "abc"
    //   解决：用 cin.fail() 检测，cin.clear() 清除错误，cin.ignore() 丢弃输入

    // 错误3：忘记包含头文件
    //   #include <iostream>  —— 使用 cout、cin 时必须包含

    // 错误4：混淆 << 和 >>
    //   << 是输出方向（程序 → 屏幕），用在 cout 上
    //   >> 是输入方向（键盘 → 程序），用在 cin 上
    //   记忆：箭头方向就是数据流动的方向

    // 错误5：用 cin >> 读取含空格的字符串
    //   cin >> 遇到空格就停止，应该用 getline 代替

    cout << endl;
    cout << "===== 输入输出演示结束 =====" << endl;

    return 0;
}
