/*
 * ============================================================
 *  主题: 一维数组详解
 * ============================================================
 *  数组是一种存储相同类型数据的连续内存空间。一维数组是
 *  最基本的数组形式。本文件涵盖:
 *    1. 数组的声明和初始化
 *    2. 使用下标 [] 访问元素
 *    3. 数组大小必须是常量 (C 风格数组)
 *    4. 数组越界 (未定义行为)
 *    5. 用 sizeof 计算数组长度
 *    6. 数组的常见操作: 遍历、求和、最值
 * ============================================================
 */

#include <iostream>
using namespace std;

int main()
{
    // ========================================
    // 1. 数组的声明和初始化
    // ========================================
    // 声明语法: 类型 数组名[大小];
    // 初始化方式有多种:

    // 方式 1: 声明后逐个赋值
    int arr1[5];
    arr1[0] = 10;
    arr1[1] = 20;
    arr1[2] = 30;
    arr1[3] = 40;
    arr1[4] = 50;

    // 方式 2: 声明时用花括号初始化
    int arr2[5] = {10, 20, 30, 40, 50};

    // 方式 3: 省略大小，由编译器根据初始化列表推断
    int arr3[] = {10, 20, 30, 40, 50};  // 大小自动为 5

    // 方式 4: 部分初始化 (未指定的元素自动为 0)
    int arr4[5] = {10, 20};   // arr4 = {10, 20, 0, 0, 0}

    // 方式 5: 全部初始化为 0
    int arr5[5] = {0};        // arr5 = {0, 0, 0, 0, 0}

    // 方式 6: C++11 统一初始化语法
    int arr6[5]{10, 20, 30, 40, 50};

    // ========================================
    // 2. 使用下标 [] 访问元素
    // ========================================
    // 数组下标从 0 开始，到 大小-1 结束
    // arr[0] 是第一个元素，arr[n-1] 是最后一个元素

    cout << "--- 访问数组元素 ---" << endl;
    int scores[5] = {85, 92, 78, 96, 88};

    // 通过下标访问
    cout << "第一个元素: scores[0] = " << scores[0] << endl;
    cout << "第三个元素: scores[2] = " << scores[2] << endl;
    cout << "最后一个:   scores[4] = " << scores[4] << endl;

    // 通过下标修改元素
    scores[0] = 90;
    cout << "修改后 scores[0] = " << scores[0] << endl;

    // 使用循环遍历数组
    cout << "所有元素: ";
    for (int i = 0; i < 5; i++)
    {
        cout << scores[i] << " ";
    }
    cout << endl;

    // ========================================
    // 3. 数组大小必须是常量 (C 风格数组)
    // ========================================
    // 在 C++ 中，C 风格数组的大小必须是编译时常量

    const int SIZE = 5;              // const 常量，可以用作数组大小
    int arr7[SIZE] = {1, 2, 3, 4, 5};

    // int n = 5;
    // int arr8[n];  // 错误! n 不是常量表达式 (在某些编译器上可能支持，但不标准)

    // C++11 可以使用 constexpr
    constexpr int ARR_SIZE = 5;
    int arr8[ARR_SIZE] = {1, 2, 3, 4, 5};

    // 注意: C++ 的 std::vector 或 std::array 可以使用变量作为大小

    // ========================================
    // 4. 数组越界 (未定义行为)
    // ========================================
    // 访问数组下标超出范围是未定义行为 (Undefined Behavior)
    // 可能导致: 程序崩溃、读取垃圾数据、安全漏洞

    int data[5] = {1, 2, 3, 4, 5};

    // 危险! 越界访问 (不要这样做):
    // cout << data[5];    // 下标 5 超出范围 (有效范围: 0~4)
    // cout << data[-1];   // 负数下标也是越界
    // data[10] = 999;     // 写入越界，可能覆盖其他变量!

    // 正确做法: 始终检查下标范围
    int index = 3;
    if (index >= 0 && index < 5)
    {
        cout << "data[" << index << "] = " << data[index] << endl;
    }
    else
    {
        cout << "下标越界!" << endl;
    }

    // ========================================
    // 5. 用 sizeof 计算数组长度
    // ========================================
    // sizeof(数组名) 返回数组占用的总字节数
    // sizeof(元素类型) 返回单个元素占用的字节数
    // 数组长度 = sizeof(数组名) / sizeof(元素类型)

    int numbers[] = {10, 20, 30, 40, 50, 60, 70};
    int length = sizeof(numbers) / sizeof(numbers[0]);

    cout << "--- sizeof 计算数组长度 ---" << endl;
    cout << "sizeof(numbers)      = " << sizeof(numbers) << " 字节" << endl;
    cout << "sizeof(numbers[0])   = " << sizeof(numbers[0]) << " 字节" << endl;
    cout << "数组长度             = " << length << endl;

    // 使用计算出的长度遍历数组
    cout << "遍历数组: ";
    for (int i = 0; i < length; i++)
    {
        cout << numbers[i] << " ";
    }
    cout << endl;

    // 注意: sizeof 在函数参数中会退化为指针，无法正确计算长度
    // 因此数组传参时需要额外传递长度参数

    // ========================================
    // 6. 数组常见操作: 求和与平均值
    // ========================================
    cout << "--- 求和与平均值 ---" << endl;
    int vals[] = {78, 85, 92, 66, 95, 88, 73};
    int len = sizeof(vals) / sizeof(vals[0]);
    int total = 0;

    for (int i = 0; i < len; i++)
    {
        total += vals[i];
    }
    double average = static_cast<double>(total) / len;
    cout << "总分: " << total << endl;
    cout << "平均分: " << average << endl;

    // ========================================
    // 7. 数组常见操作: 查找最大值和最小值
    // ========================================
    cout << "--- 最大值和最小值 ---" << endl;
    int maxVal = vals[0];      // 假设第一个元素是最大值
    int minVal = vals[0];      // 假设第一个元素是最小值
    int maxIdx = 0, minIdx = 0;

    for (int i = 1; i < len; i++)  // 从第二个元素开始比较
    {
        if (vals[i] > maxVal)
        {
            maxVal = vals[i];
            maxIdx = i;
        }
        if (vals[i] < minVal)
        {
            minVal = vals[i];
            minIdx = i;
        }
    }
    cout << "最大值: " << maxVal << " (下标 " << maxIdx << ")" << endl;
    cout << "最小值: " << minVal << " (下标 " << minIdx << ")" << endl;

    // ========================================
    // 8. 数组常见操作: 反转数组
    // ========================================
    cout << "--- 反转数组 ---" << endl;
    int rev[] = {1, 2, 3, 4, 5};
    int revLen = sizeof(rev) / sizeof(rev[0]);

    cout << "反转前: ";
    for (int i = 0; i < revLen; i++)
        cout << rev[i] << " ";
    cout << endl;

    // 首尾交换，向中间靠拢
    for (int i = 0; i < revLen / 2; i++)
    {
        int temp = rev[i];
        rev[i] = rev[revLen - 1 - i];
        rev[revLen - 1 - i] = temp;
    }

    cout << "反转后: ";
    for (int i = 0; i < revLen; i++)
        cout << rev[i] << " ";
    cout << endl;

    cout << endl << "=== 一维数组详解完毕 ===" << endl;
    return 0;
}
